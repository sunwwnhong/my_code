/****************************************************************************************************/
/*																									*/
/*	基本功能设定 Pn006																				*/
/*																									*/
/****************************************************************************************************/
void IprmcalBitParam006(void)
{

/*--------------------------------------------------------------------------------------------------*/
/*		Pn008.X : 超程(OT)警告检出选择  												            */
/*--------------------------------------------------------------------------------------------------*/ 
    switch( Prm.b_prm0_06 & 0x000F )
    {
		case 0x0000: Iprm.f.OtStopWarn = FALSE ;           break;    /* 不检出						*/
		case 0x0001: Iprm.f.OtStopWarn = TRUE ;            break;    /* 检出                        */
		default  : KpiSetParamError( &pndef_b_prm0_06 );    break;    /* 参数设置异常                */
    }
/*--------------------------------------------------------------------------------------------------*/
/*		Pn008.Y : 抱闸输出方式											            */
/*--------------------------------------------------------------------------------------------------*/   
    switch( Prm.b_prm0_06 & 0x00F0 )
    {
		case 0x0000: Iprm.f.BKsel = FALSE ;                 break;  	//内部控制		   
		case 0x0010: Iprm.f.BKsel = TRUE ;                  break;    	//外部控制 
		default  : KpiSetParamError( &pndef_b_prm0_06 );    break;     
    }
/*--------------------------------------------------------------------------------------------------*/
/*		Pn008.Z : 警告检出选择															            */
/*--------------------------------------------------------------------------------------------------*/
    switch( (Prm.b_prm0_06 >> 8) & 0x000F )
    {
        case 0x00:
            KpiSetWarningMask( WRN_OLF,      FALSE );
            KpiSetWarningMask( WRN_VIB,      FALSE );
            KpiSetWarningMask( WRN_RGOLF,    FALSE );

            KpiSetWarningMask( WRN_PGBAT,    FALSE );
            KpiSetWarningMask( WRN_OF,       FALSE );
            KpiSetWarningMask( WRN_BEOF,     FALSE );
            KpiSetWarningMask( WRN_BOOT,     FALSE );
            //KpiSetWarningMask( WRN_DBOL_LVL, FALSE );
            KpiSetWarningMask( WRN_POWEROFF,   FALSE );
            break;
        case 0x01:
            KPI_WARNINGMSK( WRN_OLF    );
            KPI_WARNINGMSK( WRN_VIB    );
            KPI_WARNINGMSK( WRN_RGOLF  );

            KPI_WARNINGMSK( WRN_PGBAT  );
            KPI_WARNINGMSK( WRN_OF     );
            KPI_WARNINGMSK( WRN_BEOF   );
            KPI_WARNINGMSK( WRN_BOOT   );
            //KPI_WARNINGMSK( WRN_DBOL_LVL );
            KPI_WARNINGMSK( WRN_POWEROFF );
            break;
        default:
            KpiSetParamError( &pndef_b_prm0_06 );
            break;
    }
/*--------------------------------------------------------------------------------------------------*/
/*		Pn008.W : 风扇温度控制															            */
/*--------------------------------------------------------------------------------------------------*/        
    switch( (Prm.b_prm0_06 >> 12) & 0x000F )
    {
        case 0: Iprm.FanContMode = 0;   break;
        case 1: Iprm.FanContMode = 1;   break;
        case 2: Iprm.FanContMode = 2;   break;
        case 3: Iprm.FanContMode = 3;   break;
        case 4: Iprm.FanContMode = 4;   break;
        default:KpiSetParamError( &pndef_b_prm0_06 );break;
    }
}


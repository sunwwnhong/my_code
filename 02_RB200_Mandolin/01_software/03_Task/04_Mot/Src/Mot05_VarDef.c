/****************************************************************************************************/
/*																									*/
/*	基本功能设定 Pn00A																				*/
/*																									*/
/****************************************************************************************************/
void IprmcalBitParam00A(void)
{
/*--------------------------------------------------------------------------------------------------*/
/*		AC/DC 电源输入选择                															*/
/*--------------------------------------------------------------------------------------------------*/
    switch( Prm.b_prm0_0D & 0x000F ) 
    {
        /*------------------------------------------------------------------------------------------*/
        case 0x0000:                                /* AC电源输入：从L1、L2、L3端子输入AC电源       */
            Iprm.f.Dcbus = FALSE;
            break;
        /*------------------------------------------------------------------------------------------*/
        case 0x0001:                                /* DC电源输入：从“+”、“-”之间输入DC电源         */
            Iprm.f.Dcbus = TRUE;
            break;
        /*------------------------------------------------------------------------------------------*/
        default:                                    /* 参数设定异常                                 */
            KpiSetParamError( &pndef_b_prm0_0D );
            break;        
    }
  
/*--------------------------------------------------------------------------------------------------*/
/*		三相输入规格伺服单元的单元输入选择															*/
/*--------------------------------------------------------------------------------------------------*/
    switch( Prm.b_prm0_0D & 0x00F0 )
    {
        /*--------------------------------------------------------------------------------------*/
        case 0x0000:     /* 以三相电源输入使用             */
            Iprm.f.SnglPhBaseDerate = FALSE;
            break;
        /*--------------------------------------------------------------------------------------*/
        case 0x0010:	/* 以单相电源输入使用三相输入规格  */
            Iprm.f.SnglPhBaseDerate = TRUE;
            break;
        /*--------------------------------------------------------------------------------------*/
        default:        /* 参数设定异常                    */
            KpiSetParamError( &pndef_b_prm0_0D );
            break;
    }
   
/*--------------------------------------------------------------------------------------------------*/
/*		电机速度检出方式																			*/
/*--------------------------------------------------------------------------------------------------*/
    switch( Prm.b_prm0_0D & 0x0F00 )
    {
        /*--------------------------------------------------------------------------------------*/
        case 0x0000:    /* 速度检测方式1*/
            Iprm.f.SpdDetMaFil = FALSE;
            break;
        /*--------------------------------------------------------------------------------------*/
        case 0x0100:	/* 速度检测方式2  使用滤波*/
        case 0x0200:
        case 0x0300:
            Iprm.f.SpdDetMaFil = TRUE;
            break;
        /*--------------------------------------------------------------------------------------*/
        default:	/* 参数设定异常 */
            KpiSetParamError( &pndef_b_prm0_0D );
            break;
    }
        
/*--------------------------------------------------------------------------------------------------*/
/*		绝对位置软限位检测功能开关																	*/
/*--------------------------------------------------------------------------------------------------*/
    switch( Prm.b_prm0_0D & 0xF000 )
    {
        /*--------------------------------------------------------------------------------------*/
        case 0x0000:                        /* 软限位功能关闭 */
            Iprm.f.AbsPosLimit = 0;
            break;
        /*--------------------------------------------------------------------------------------*/
        case 0x1000:	                    /*  软限位功能开启 内部软件 */
            Iprm.f.AbsPosLimit = 1;
            break;
        /*--------------------------------------------------------------------------------------*/
        case 0x2000:	                    /*  软限位功能开启 EtherCat */
            Iprm.f.AbsPosLimit = 2;
            break;
        /*--------------------------------------------------------------------------------------*/
        default:
            KpiSetParamError( &pndef_b_prm0_0D );
            break;
        /*--------------------------------------------------------------------------------------*/
    }
}


/****************************************************************************************************/
/*																									*/
/*	基本功能设定 Pn040	绝对值编码器使用方法														*/
/*																									*/
/****************************************************************************************************/
void IprmcalBitParam040( void )
{
    /*------------------------------------------------------------------------------------------*/
    /* 标准脉冲型                                                                               */
    /*------------------------------------------------------------------------------------------*/
    if( Kprm.f.EtherCATNet == 0 )
    {
        switch(Prm.absEncuse & 0x000F)
        {
            /*----------------------------------------------------------------------------------*/
            case 0x0000:/* 正常使用多圈编码器 */
                Iprm.f.MulTSingT = 0x0;
                break;
            /*----------------------------------------------------------------------------------*/
            case 0x0001:/* 多圈编码器当做增量式使用 */
                Iprm.f.MulTSingT = 0x1;
                break;
            /*----------------------------------------------------------------------------------*/
            default:	/* 参数设定异常 */
                KpiSetParamError( &pndef_absEncuse );
                break;
        }
    }
    /*------------------------------------------------------------------------------------------*/
    /* 总线型驱动器                                                                             */
    /*------------------------------------------------------------------------------------------*/
    else
    {
        switch(Prm.absEncuse & 0x00F0)
        {
            /*----------------------------------------------------------------------------------*/
            case 0x0000:/* 正常使用多圈编码器 */
                Iprm.f.MulTSingT = 0x0;
                break;
            /*----------------------------------------------------------------------------------*/
            case 0x0010:/* 多圈编码器当做增量式使用 */
                Iprm.f.MulTSingT = 0x1;
                Iprm.f.MulTSing  = 0x0;
                break;
            case 0x0020:/* 单圈绝对值编码器                                                     */
                Iprm.f.MulTSingT = 0x1;
                Iprm.f.MulTSing  = 0x1;
                break;
            /*----------------------------------------------------------------------------------*/
            default:	/* 参数设定异常 */
                KpiSetParamError( &pndef_absEncuse );
                break;
        }
    }
    /*------------------------------------------------------------------------------------------*/
    /* 进入测试模式1 或者测试模式2 时,不对编码器电池进行校验处理                                */
    /*------------------------------------------------------------------------------------------*/
    if( Iprm.FATest1 == 1  || Iprm.FATest2 == 1 )
    {
        Iprm.f.MulTSingT = 0x1;                          /* 多圈绝对值编码器当增量式编码器使用  */
    }
}


/* Function_Name: VarDef_Init        */
/* Description  : 初始化变量处理清零处理
                  调用：Main函数*/
/**********************************************************************************************/
void VarDef_Init(void)
{
    /*---------------------基础系统参数初始化--------------------------------------------------------*/
    MlibResetLONGMemory( &Iprm, sizeof(Iprm) / 2 );
    MlibResetLONGMemory( &Gprm, sizeof(Gprm) / 2 );					 
    MlibResetLONGMemory( &AinK, sizeof( AinK ) / 2 );
    MlibResetLONGMemory( &AoutV,sizeof( AoutV ) / 2 );
    MlibResetLONGMemory( &BoutK, sizeof(BoutK) / 2 );
    MlibResetLONGMemory( &BinV, sizeof(BinV) / 2 );					 
    MlibResetLONGMemory( &BoutV, sizeof(BoutV) / 2 );				 
    MlibResetLONGMemory( &BoutRrst, sizeof(BoutRrst) / 2 );
    MlibResetLONGMemory( &CinV, sizeof(CinV) / 2 );
    MlibResetLONGMemory( &CoutV, sizeof(CoutV) / 2 );				 
    MlibResetLONGMemory( &CoutRrst, sizeof(CoutRrst) / 2 );			 
    MlibResetLONGMemory( &CinK, sizeof(CinK) / 2 );                  
    DriveSeriesSet( );                  // 驱动器系列设置
    ApxInitRippleComp();                // 复位速度脉动补偿计算变量
    LpxInitEhSpeedObserver();           // 相位补偿速度观测器变量初始化
    InitSpeedObserver( );               // 速度观测器变量初始化，考虑删除
    SoftStart_Init( );                  // 速度S曲线参数初始化
    AI12DataInit( );
}

/**********************************************************************************************/
/* Function_Name: IprmcalBitParam000        */
/* Description  : 功能选择基本开关(Pn000)
                  调用：Main函数*/
/**********************************************************************************************/
void	IprmcalBitParam000( void )
{
  
/*--------------------------------------------------------------------------------------------------*/
/*		Pn000.X : 控制模式选择																		*/
/*--------------------------------------------------------------------------------------------------*/
    switch( Prm.b_prm0_0 & 0x000F )
    {
        case 0x00:                          /* 位置控制(脉冲序列指令)		                    */
             Iprm.CtrlModeA = CTRLMODE_POS;
             Iprm.CtrlModeB = CTRLMODE_POS;
             Iprm.CtrlMode  = 0;
             break;
        case 0x01:                          /* 速度控制模式					                    */
             Iprm.CtrlModeA = CTRLMODE_SPD;
             Iprm.CtrlModeB = CTRLMODE_SPD;
             Iprm.CtrlMode  = 1;
             break;
        case 0x02:                          /* 转矩控制						                    */
             Iprm.CtrlModeA = CTRLMODE_TRQ;
             Iprm.CtrlModeB = CTRLMODE_TRQ;
             Iprm.CtrlMode  = 2;
             break;
        case 0x03:                          /* 速度控制模式 <--> 位置控制模式	                */
             Iprm.CtrlModeA = CTRLMODE_SPD;
             Iprm.CtrlModeB = CTRLMODE_POS;
             Iprm.CtrlMode  = 3;
             break;
        case 0x04:                          /* 转矩控制模式 <--> 位置控制模式	                */
             Iprm.CtrlModeA = CTRLMODE_TRQ;
             Iprm.CtrlModeB = CTRLMODE_POS;
             Iprm.CtrlMode  = 4;
             break;
        case 0x05:                          /* 速度控制模式 <--> 转矩控制模式	                */
             Iprm.CtrlModeA = CTRLMODE_SPD;
             Iprm.CtrlModeB = CTRLMODE_TRQ;
             Iprm.CtrlMode  = 5;
             break;
        case 0x06:                          /* 速度控制模式 <--> 位置控制模式 <--> 转矩控制模式 */
             Iprm.CtrlModeA = CTRLMODE_SPD;
             Iprm.CtrlModeB = CTRLMODE_POS;
             Iprm.CtrlModeC = CTRLMODE_TRQ;
             Iprm.CtrlMode  = 6;
             break;
        case 0x07:
             Iprm.CtrlModeA = CTRLMODE_VF;
             Iprm.CtrlModeB = CTRLMODE_VF;
             Iprm.CtrlMode  = 7;
             break;
        case 0x08:
            Iprm.CtrlModeA = CTRLMODE_POS_MIT;
            Iprm.CtrlModeB = CTRLMODE_POS_MIT;
            Iprm.CtrlMode  = 8;
            break;
        default:
             KpiSetParamError( &pndef_b_prm0_0 );
             Iprm.CtrlMode  = 0;
             break;
    }
    Iprm.CtrlModeInit = CTRLMODE_SPD;
    /*------------------------------------------------------------------------------------------*/
    if( Prm.Testpassword == 3322 )  /* 整机老化测试时,强制进入VF模式 */
    {
        Iprm.CtrlModeA = CTRLMODE_VF;
        Iprm.CtrlModeB = CTRLMODE_VF;
    }
/*------------------------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------------------------------*/
/*		Pn000.Y : 保留														                        */
/*--------------------------------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------------------------------*/
/*		Pn000.Z : 机型选择																		    */
/*--------------------------------------------------------------------------------------------------*/
	switch( (Prm.b_prm0_0 >> 8) & 0x000F )
	{
        case 0:                              
            Kprm.f.CanOpenNet   = FALSE;
            Kprm.f.EtherCATNet  = FALSE;
            Kprm.f.StandardType = TRUE;
                
             break;
        case 1:                              
            Kprm.f.CanOpenNet   = TRUE;
            Kprm.f.EtherCATNet  = FALSE;
            Kprm.f.StandardType = FALSE;
            break;
        case 2:                              
            Kprm.f.CanOpenNet   = FALSE;
            Kprm.f.EtherCATNet  = TRUE;
            Kprm.f.StandardType = FALSE;
            break;
        default:
            Kprm.f.CanOpenNet   = FALSE;
            Kprm.f.EtherCATNet  = FALSE;
            Kprm.f.StandardType = TRUE;
            KpiSetParamError( &pndef_b_prm0_0 );
            break;
    }
	/*------------------------------------------------------------------------------------------*/
    if( Prm.Testpassword == 3322 )
    {
        Kprm.f.CanOpenNet   = FALSE;
        Kprm.f.EtherCATNet  = FALSE;
        Kprm.f.StandardType = TRUE;
    }

    /*------------------------------------------------------------------------------------------*/
    /*  无电机测试模式                                                                          */
    /*------------------------------------------------------------------------------------------*/
    if( ((Prm.b_prm0_0 >> 12) & 0x000F ) == 0x0002 )
    {
        Kprm.f.MotorLessTestMode = 1;
    }
    else
    {
        Kprm.f.MotorLessTestMode = 0;
    }
    return;
}
  
  

/*		电压检测滤波器选择开关设定(PnE14)														    */
/*--------------------------------------------------------------------------------------------------*/
/*																									*/
/*		PnE7A.Bit0,1 : 主电路电压检测异常检测滤波器		( 00-11 / 0us-750us , 250us )			*/
/*		PnE7A.Bit2,3 : 过电压报警检测滤波器				( 00-11 / 0us-750us , 250us)			*/
/*		PnE7A.Bit4,5 : 再生动作开始滤波器				( 00-11 / 0us-750us , 250us )			*/
/*		PnE7A.Bit6,7 : 再生动作结束滤波器				( 00-11 / 0us-750us , 250us )			*/
/*--------------------------------------------------------------------------------------------------*/
 void	IprmcalVdetfilSwitch( void )
{
USHORT	swbuf;
    swbuf = Prm.vdefilsw;
/*--------------------------------------------------------------------------------------------------*/
/*		各种过滤器设置							                                                */
/*--------------------------------------------------------------------------------------------------*/

    Iprm.VdcdetAlmFilter	= swbuf & 0x0007;			/* 主电路检测异常报警滤波器		*/
    Iprm.OvdetAlmFilter		= ( swbuf >> 4 ) & 0x0007;	/* 过电压报警检测滤波器			*/
    Iprm.RegStartFilter		= ( swbuf >> 8 ) & 0x0007;	/* 再生动作开始滤波器					*/
    Iprm.RegStopFilter		= ( swbuf >> 12 ) & 0x0007;	/* 再生动作结束滤波器					*/
    return;

}



/****************************************************************************************************/
/*																									*/
/*	基本功能设定 Pn004 伺服Off & Gr.1时停机方式														*/
/*																									*/
/****************************************************************************************************/
void IprmcalBitParam004(void)
{
/*--------------------------------------------------------------------------------------------------*/
/*		Pn006.X : 伺服OFF时的停止方法													            */
/*--------------------------------------------------------------------------------------------------*/  
    switch( Prm.b_prm0_04 & 0x000F )
    {
		case 0x0000:        /* 通过DB(动态制动器)来停止电机   */
          Iprm.OffStopMode = STOPMODE_DBSTOPDB;
          Iprm.StopMode = STOPMODE_DBSTOPDB;
          break;
		case 0x0001:        /* 通过DB停止电机，然后解除DB     */
          Iprm.OffStopMode = STOPMODE_DBSTOPFR;
          Iprm.StopMode = STOPMODE_DBSTOPFR;
          break;
		case 0x0002:        /* 不使用DB,将电机设为自由运行状态*/
          Iprm.OffStopMode = STOPMODE_FREERUN;
          Iprm.StopMode = STOPMODE_FREERUN;
          break;
        case 0x0003:        /* 减速停机                       */
          Iprm.OffStopMode = STOPMODE_LDECSTOP; 
          break;
		default:            /* 参数设置异常                   */
          KpiSetParamError( &pndef_b_prm0_04 );   
          break;
    }
}


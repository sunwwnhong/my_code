#include "Main.h"



//*************************************************************************************************
//
// ms 延时
//
//*************************************************************************************************
void KlibWaitms(ULONG time)
{
    DEVICE_DELAY_US( 1000UL * time );
}

//*************************************************************************************************
//
// us延时
//
//*************************************************************************************************
void KlibWaitus(ULONG time)
{
    DEVICE_DELAY_US( time );
}



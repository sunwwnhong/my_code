#include "Main.h"


//*************************************************************************************************
//
// 再生制定电阻初始状态 -- OFF
//
//*************************************************************************************************
void BKControl_Init(void)
{
#ifdef CPU1
    KPX_BRAKE_OFF();
#endif
}


//*************************************************************************************************
//
// 再生制定电阻状态 -- On
//
//*************************************************************************************************
void BKOn(void)
{
#ifdef CPU1
    if( StateM_Var.f.EepromError == 1 )
    {
        KPX_BRAKE_OFF();
    }
    else
    {
        KPX_BRAKE_ON();
    }
#endif
}

//*************************************************************************************************
//
// 再生制定电阻状态 -- Off
//
//*************************************************************************************************
void BKOff(void)
{
#ifdef CPU1
    KPX_BRAKE_OFF();
#endif
}


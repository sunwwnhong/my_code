#include "Main.h"


//*************************************************************************************************
//
// PWM ON
//
//*************************************************************************************************
void PWM_Off(void)
{
    SysBitDef.f.ServoOnOff = 0;
}

//*************************************************************************************************
//
// PWM OFF
//
//*************************************************************************************************
void PWM_On(void)
{
    if( Kprm.f.MotorLessTestMode == FALSE )
    {
        SysBitDef.f.ServoOnOff = 1;
    }
}

//*************************************************************************************************
//
// Mos밗菱앨밑균
//
//*************************************************************************************************
void Mosfet_PumpOff(void)
{
    SysBitDef1.f.MossfetEn = 0;
}


//*************************************************************************************************
//
// Mos밗菱앨댔역
//
//*************************************************************************************************
void Mosfet_PumpOn(void)
{
    if( Kprm.f.MotorLessTestMode == FALSE )
    {
        SysBitDef1.f.MossfetEn = 1;
    }
}





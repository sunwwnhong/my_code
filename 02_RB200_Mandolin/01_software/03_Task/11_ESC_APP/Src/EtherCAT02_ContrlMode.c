#include "Main.h"


UCHAR EtherCAT_ControlMode = 0;

/****************************************************************************************************/
/*Function_Name:EtherCAT_ControlModeSel()                                                           */
/*Description  :EtherCAT 왠齡친駕朞嶝                                                                  */
/****************************************************************************************************/
void EtherCAT_ControlModeSel(void)
{
	CinV.f.Sv.OTCheck = 0;  
    switch( s8ModeofOP )
    {
        /*------------------------------------------------------------------------------------------*/
        case 1:                                                 /* 貫零쫴윌친駕( 1 )                */
            s8ModeofOPDis = 1;
            EtherCAT_ControlMode = 1;
            break;
        /*------------------------------------------------------------------------------------------*/
        case 3:                                                 /* 醵똑쫴윌친駕( 3 )                */
            s8ModeofOPDis = 3;
            EtherCAT_ControlMode = 0;
            EtherCAT_SpdAccDec();
            SpdCoeffUpdate();
            AbsPosRecord = 0;
            break;
        /*------------------------------------------------------------------------------------------*/
        case 4:                                                 /* 瘻앤쫴윌친駕( 4 )                */
            s8ModeofOPDis = 4;
            EtherCAT_ControlMode = 2;
            AbsPosRecord = 0;
            break;
        /*------------------------------------------------------------------------------------------*/            
        case 6:                                                 /* 쀼쥐친駕( 6 )                    */
            s8ModeofOPDis = 6;
            EtherCAT_ControlMode = 1;
            CinV.f.Sv.OTCheck = 1;                              /* 낚넋쇱꿎                         */
            if(BinV.f.BaseEnable)
            {
                if(Prm.b_prm0_06 & 0x0001 ==0x0001)
                {
                    if(CinV.f.Sv.PotIn==1)
                    {
                        KPI_WARNINGSET(WRN_POT);
                    }
                    else if(CinV.f.Sv.NotIn==1)
                    {
                        KPI_WARNINGSET(WRN_NOT);
                    }
                }
            }
            break;
        /*------------------------------------------------------------------------------------------*/            
        case 8:                                                 /* 鷺퍅谿꼍貫零친駕( 8 )            */
            s8ModeofOPDis = 8;
            EtherCAT_ControlMode = 1;
            AbsPosRecord = 0;
            break;
        /*------------------------------------------------------------------------------------------*/            
        case 9:                                                 /* 鷺퍅谿꼍醵똑친駕( 9 )            */
            s8ModeofOPDis = 9;
            EtherCAT_ControlMode = 0;
            AbsPosRecord = 0;
            break;
        /*------------------------------------------------------------------------------------------*/            
        case 10:                                                /* 鷺퍅谿꼍瘻앤친駕( 10 )           */
            s8ModeofOPDis = 10;
            EtherCAT_ControlMode = 2;
            AbsPosRecord = 0;
            break;
    /*------------------------------------------------------------------------------------------*/
       case 5:                                                /* 제貫살북왠齡친駕( 5 )           */
           s8ModeofOPDis = 5;
           EtherCAT_ControlMode = 7;
           AbsPosRecord = 0;
           break;
        /*------------------------------------------------------------------------------------------*/            
        default:
            EtherCAT_ControlMode = 0;
            s8ModeofOPDis = s8ModeofOP;                         /* 페劍친駕(嫩끽)                   */
            break;
    }
    
    /*----------------------------------------------------------------------------------------------*/
    /*  렷貫零왠齡친駕珂,貫零宮밑긴좆헌쥐샀諒뫘劤뇹잿                                               */
    /*----------------------------------------------------------------------------------------------*/
    if( EtherCAT_ControlMode != 1 )
    {
        EtherCATPosDeal(  );
    }

}


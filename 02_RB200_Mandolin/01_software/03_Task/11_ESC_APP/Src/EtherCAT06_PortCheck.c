#include "Main.h"
#ifdef CPU1
#include "EtherCAT01_2838x.h"
#endif

DATALINK_VAL DataLinkV;



//*************************************************************************************************
// Function Name: DataLink_Init( void )
// Function Description: 数据链接错误计数初始化
//*************************************************************************************************
void DataLink_Init( void )
{
    MlibResetSHORTMemory((void *)&DataLinkV, sizeof(DataLinkV));
}

//*************************************************************************************************
// Function Name: DataLink_Deal( void )
// Function Description: 数据链接错误计数处理
//*************************************************************************************************
void DataLink_Deal( void )
{
#ifdef CPU1
USHORT kx;
    switch( DataLinkV.Step)
    {
        //-----------------------------------------------------------------------------------------
        // 端口0 : 接收错误计数(高8位) | 无效帧计数
        //-----------------------------------------------------------------------------------------
        case 0:
            kx = ESC_readWord( 0x0300 );
            DataLinkV.Un045 = (SHORT)(( kx >> 0 ) & 0x00FF);
            DataLinkV.Un046 = (SHORT)(( kx >> 8 ) & 0x00FF);
            //-------------------------------------------------------------------------------------
            DataLinkV.Step ++;                                         // 下一步
        break;
        //-----------------------------------------------------------------------------------------
        // 端口1 : 接收错误计数(高8位) | 无效帧计数
        //-----------------------------------------------------------------------------------------
        case 1:
            kx = ESC_readWord( 0x0302 );
            DataLinkV.Un047 = (SHORT)(( kx >> 0 ) & 0x00FF);
            DataLinkV.Un048 = (SHORT)(( kx >> 8 ) & 0x00FF);
            //-------------------------------------------------------------------------------------
            DataLinkV.Step ++;                                        // 下一步
        break;
        //-----------------------------------------------------------------------------------------
        // 端口转发错误计数 : 端口1(高8位) | 端口0(低8位)
        //-----------------------------------------------------------------------------------------
        case 2:
            kx = ESC_readWord( 0x0308 );
            DataLinkV.Un049 = (SHORT)(( kx >> 0 ) & 0x00FF);
            DataLinkV.Un04A = (SHORT)(( kx >> 8 ) & 0x00FF);
            //-------------------------------------------------------------------------------------
            DataLinkV.Step ++;                                       // 下一步
        break;
        //-----------------------------------------------------------------------------------------
        // PDI错误计数器(高8位) | 数据帧处理单元错误计数(低8位)
        //-----------------------------------------------------------------------------------------
        case 3:
            kx = ESC_readWord( 0x030C );
            DataLinkV.Un04B = (SHORT)(( kx >> 0 ) & 0x00FF);
            DataLinkV.Un04C = (SHORT)(( kx >> 8 ) & 0x00FF);
            //-------------------------------------------------------------------------------------
            DataLinkV.Step ++;                                       // 下一步
        break;
        //-----------------------------------------------------------------------------------------
        // 端口链接错误计数 : 端口1(高8位) | 端口0(低8位)
        //-----------------------------------------------------------------------------------------
        case 4:
            kx = ESC_readWord( 0x0310 );
            DataLinkV.Un04D = (SHORT)(( kx >> 0 ) & 0x00FF);
            DataLinkV.Un04E = (SHORT)(( kx >> 8 ) & 0x00FF);
            //-------------------------------------------------------------------------------------
            DataLinkV.Step ++;                                       // 下一步
        break;
        //-----------------------------------------------------------------------------------------
        default:
            DataLinkV.Step = 0;
        break;  
    }
#endif
}
















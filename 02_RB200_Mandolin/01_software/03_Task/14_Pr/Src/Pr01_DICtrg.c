/****************************************************************************************************/
/*																									*/
/*		PR模式之DI端子选择位置指令的相关信息														*/
/*                                                  												*/
/****************************************************************************************************/
void InnerPosInformation( USHORT Target )
{
USHORT KX;
USHORT KXL,KXH;
    /*----------------------------------------------------------------------------------------------*/
    Pr_DICtrgV.PosNum       = Target;
    /*----------------------------------------------------------------------------------------------*/
    KX = *( &Prm.PathPlan1L + (Target-1) * 4);  
    Pr_DICtrgV.PosType      = KX & 0x0001;                       //指令类型
    Pr_DICtrgV.PosOption    = ( KX >> 4 ) & 0x0003;              //位置类型
    Pr_DICtrgV.SpeedUnit    = ( KX >> 8 ) & 0x0001;              //速度单位
    KX = *( &Prm.PathPlan1H + (Target-1) * 4);
    Pr_DICtrgV.PrAccTime    = *( &Prm.AccDecT0 + ((KX >> 0 ) & 0x000F));                 // 加速时间(ms) 
    Pr_DICtrgV.PrDecTime    = *( &Prm.AccDecT0 + ((KX >> 4 ) & 0x000F));                 // 减速时间(ms)
    Pr_DICtrgV.PrSpd        = *( &Prm.Spd0     + ((KX >> 8 ) & 0x000F));                 // 目标速度(0.1rpm)
    Pr_DICtrgV.PrDelayTime  = (ULONG)8 * (*( &Prm.DelayT0  + ((KX >> 12) & 0x000F)));    // 延时时间(ms)
    /*----------------------------------------------------------------------------------------------*/
    KXL = *( &Prm.PathPlan2L + (Target-1) * 4);                          // Pr1资料，POS 低16位
    KXH = *( &Prm.PathPlan2H + (Target-1) * 4);                          // Pr1资料，POS 高16位
    Pr_DICtrgV.PrPos = LONGOF( KXL,KXH );
    
}


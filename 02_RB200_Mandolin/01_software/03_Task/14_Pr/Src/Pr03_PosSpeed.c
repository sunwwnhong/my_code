PR_POSSPEED PrPosSpdP;


/****************************************************************************************************/
/*																									*/
/*		ЮЛжУЫйЖШаХЯЂЕФМЦЫуДІРэ  																	*/
/*  Attention: МгЫйКЭМѕЫйЕФЪБМфОљЪЧЛљгк[0,3000] rpmРДМЦЫу                      						*/
/*																									*/
/****************************************************************************************************/
void PrPosSpdCoeffCount( void )
{
    PrSpeed_AccDec_CoefCount();                 //МЦЫуМгМѕЫйЛљжЕ
    /*----------------------------------------------------------------------------------------------*/
    /* зюДѓЫйЖШМЦЫу( ЕЅЮЛЗжГЩ0.1rpmКЭppsСНжж )                                                       */
    /*----------------------------------------------------------------------------------------------*/
    if( Pr_DICtrgV.SpeedUnit )
    {
        PrPosSpdP.ProfRefSpdQ15 = (LLONG)Pr_DICtrgV.PrPos * 512 / 125;         // ЕЅЮЛЮЊpps
    }
    else
    {
        PrPosSpdP.ProfRefSpdQ15 = ((LLONG)EGearDen<<12)*Pr_DICtrgV.PrPos / (600000);  // ЕЅЮЛЮЊ0.1rpm
    }
    /*----------------------------------------------------------------------------------------------*/
    /* зюДѓЫйЖШЯожЦ( 6000rpm )                                                       */
    /*----------------------------------------------------------------------------------------------*/
//    if(PrPosSpdP.ProfRefSpdQ15>PrModeSpdMax || PrPosSpdP.ProfRefSpdQ15<(-PrModeSpdMax))
//    {
//        PrPosSpdP.ProfRefSpdQ15 = PrModeSpdMax;
//    }
    /*----------------------------------------------------------------------------------------------*/
    /* ЮЛжУПижЦМгМѕЫйЛљжЕМЦЫу                                                                       */
    /*----------------------------------------------------------------------------------------------*/
    if( Pr_DICtrgV.PrAccTime == 0 )
    {
        PrPosSpdP.AccBaseQ15 = ACCDECCoeffQ15;
    }
    else
    {
        PrPosSpdP.AccBaseQ15 = (ACCDECCoeffQ15>>3) / Pr_DICtrgV.PrAccTime; 
        if(PrPosSpdP.AccBaseQ15 <= 1)
        {
            PrPosSpdP.AccBaseQ15 = 1;
        }
    }
    /*----------------------------------------------------------------------------------------------*/
    if( Pr_DICtrgV.PrDecTime == 0 )
    {
        PrPosSpdP.DecBaseQ15 = ACCDECCoeffQ15;
    }
    else
    {
        PrPosSpdP.DecBaseQ15 = (ACCDECCoeffQ15>>3) / Pr_DICtrgV.PrDecTime;    
        if(PrPosSpdP.DecBaseQ15 <= 1)
        {
            PrPosSpdP.DecBaseQ15 = 1;
        }
    }
}

/****************************************************************************************************/
/*																									*/
/*		PRФЃЪНжЎЖЈЫйПижЦ        																	*/
/*                                                  												*/
/****************************************************************************************************/
LONG PrPosSpeedDeal( LONG PosSpd_Q15 )
{
    /*----------------------------------------------------------------------------------------------*/
    /* ЕБЧАЕФЫйЖШДѓгкЕШгк0                                                                          */
    /*----------------------------------------------------------------------------------------------*/
    if( PrPosSpdP.ProfRefSpdQ15 >= 0 )
    {
        /*------------------------------------------------------------------------------------------*/
        if( PosSpd_Q15 >= 0 )
        {
            /*--------------------------------------------------------------------------------------*/
            if( PosSpd_Q15 > PrPosSpdP.ProfRefSpdQ15 )
            {
                PosSpd_Q15 -= PrPosSpdP.DecBaseQ15;
                if( PosSpd_Q15 < PrPosSpdP.ProfRefSpdQ15 ) PosSpd_Q15 = PrPosSpdP.ProfRefSpdQ15;
            }
            /*--------------------------------------------------------------------------------------*/
            else if( PosSpd_Q15 < PrPosSpdP.ProfRefSpdQ15 )
            {
                PosSpd_Q15 += PrPosSpdP.AccBaseQ15;
                if( PosSpd_Q15 > PrPosSpdP.ProfRefSpdQ15 ) PosSpd_Q15 = PrPosSpdP.ProfRefSpdQ15;
            }
            /*--------------------------------------------------------------------------------------*/
            else
            {
                PosSpd_Q15 = PrPosSpdP.ProfRefSpdQ15;
                /*----------------------------------------------------------------------------------*/
                /* ФПБъЫйЖШМгЫйЭъГЩ                                                                 */
                /*----------------------------------------------------------------------------------*/
                Pr_DICtrgV.f.PosSpdReachflag = 1;
            }
        }
        /*------------------------------------------------------------------------------------------*/
        else
        {
            /*--------------------------------------------------------------------------------------*/
            /*  ЕБФПБъЫйЖШЮЊ Ён0 ЕФжЕЪБ,ЖјЕБЧАЕФЫйЖШ <0 ЪБ,дђЕБЧАЕФЫйЖШашвЊНјааМѕЫйДІРэ              */
            /*--------------------------------------------------------------------------------------*/
            PosSpd_Q15 += PrPosSpdP.DecBaseQ15;
        }
    }
    /*----------------------------------------------------------------------------------------------*/
    /* ЕБФПБъЫйЖШ <0 ЪБ                                                                             */
    /*----------------------------------------------------------------------------------------------*/
    else
    {
        if( PosSpd_Q15 <= 0 )
        {
            /*--------------------------------------------------------------------------------------*/
            if( PosSpd_Q15 > PrPosSpdP.ProfRefSpdQ15 )
            {
                PosSpd_Q15 -= PrPosSpdP.AccBaseQ15;
                if( PosSpd_Q15 < PrPosSpdP.ProfRefSpdQ15 ) PosSpd_Q15 = PrPosSpdP.ProfRefSpdQ15;
            }
            /*--------------------------------------------------------------------------------------*/
            else if( PosSpd_Q15 < PrPosSpdP.ProfRefSpdQ15 )
            {
                PosSpd_Q15 += PrPosSpdP.DecBaseQ15;
                if( PosSpd_Q15 > PrPosSpdP.ProfRefSpdQ15 ) PosSpd_Q15 = PrPosSpdP.ProfRefSpdQ15;
            }
            /*--------------------------------------------------------------------------------------*/
            else
            {
                PosSpd_Q15 = PrPosSpdP.ProfRefSpdQ15;
                /*----------------------------------------------------------------------------------*/
                /* ФПБъЫйЖШМгЫйЭъГЩ                                                                 */
                /*----------------------------------------------------------------------------------*/
                Pr_DICtrgV.f.PosSpdReachflag = 1;
            }
        }
        else
        {
            PosSpd_Q15 -= PrPosSpdP.DecBaseQ15;
        }
    }
    return PosSpd_Q15;
}












LONG PrModeMain( void )
{
LONG   KX = 0;
USHORT  PrModeSet = Prm.PrCmdSet & 0x000F;
/*--------------------------------------------------------------------------------------------------*/
/*                                  非位置模式时直接退出                                            */
/*--------------------------------------------------------------------------------------------------*/
        if( BPI_GET_BASE_CTRLMODE( ) != CTRLMODE_POS )
        {
            return 0;
        }
/*--------------------------------------------------------------------------------------------------*/
/*                                      PR 指令来源选择                                              */
/*--------------------------------------------------------------------------------------------------*/
        switch( PrModeSet )
        {
            /*--------------------------------------------------------------------------------------*/
            case 0x0000:            /* PR—— 内部多段位置                  */
                KX = PrMultiPos();
                break;
           
            default:
                KX = 0;
                break;
            /*--------------------------------------------------------------------------------------*/
        }
        
        return KX;
}

LONG PrMultiPos()
{
    LONG   KX = 0;
    USHORT  PrPosModeSet = Prm.PrModeSet & 0x000F;
    
    Pr_DI_ConTrig();
    
    if( !BinV.f.BaseEnable )
    {
        Pr_DICtrgV.StopSign = 1;
        return 0;
    }
    if(!OrgControlBit.bit.OrgDone)
    {
        Pr_DICtrgV.PosNum = 0;
        return 0;
    }
    switch( PrPosModeSet )
    {
        /*--------------------------------------------------------------------------------------*/
        case 0x0000:            // PR—— 单段运行，端子或通讯给定                   
            KX = PrModeSinglePos();
            break;
        /*--------------------------------------------------------------------------------------*/
        case 0x0001:            //PR—— 单次顺序运行，执行完成后停止
            KX = PrModeSeveralPos();
            break;
        /*--------------------------------------------------------------------------------------*/
        case 0x0002:            //PR——循环顺序运行,执行完成后重新开始
            KX = PrModeCirclePos();
            break;
        /*--------------------------------------------------------------------------------------*/
        case 0x0003:            //PR——顺序运行，第一轮结束后，起始地址从Pn804设定的开始循环运行
            KX = PrModeSequencePos();
            break;                
        /*--------------------------------------------------------------------------------------*/
//            case 0x0004:            //PR——分度功能
//                break;
        /*--------------------------------------------------------------------------------------*/
        default:
            KX = 0;
            break;
        /*--------------------------------------------------------------------------------------*/
    }
    return KX;
}

ULONG	KpiRstLongTimer( ULONG *timer )
{
		*timer = KnlV.ScanBcnt;
		return( 0 );
}


/****************************************************************************************************/
/*  时间计时器                                                                                      */
/****************************************************************************************************/
ULONG	KpiGetLongTimer( ULONG *timer )
{
ULONG	timex;

		timex = KnlV.ScanBcnt - *timer;
		return( KPX_SBTIMER1MS( timex ) );
}

LONG	RpxReadOpeRegister( LONG RegNo, LONG Number, USHORT *RdBuf, HMSGIF Hmsg )
{
  
LONG	i, rc;																						
CFORDEF	*OpeRegTbl;
LONG	Rno = RegNo & 0xFFF;
USHORT OpRegData;
/*--------------------------------------------------------------------------------------------------*/
/*		Check the trace data																		*/
/*--------------------------------------------------------------------------------------------------*/
		LpxCheckDataTraceEnd( Hmsg );						/* Trace data check processing			*/

/*--------------------------------------------------------------------------------------------------*/
/*		レジスタ番号検索																			        */
/*--------------------------------------------------------------------------------------------------*/
		if( (OpeRegTbl = LpxSearchFunOpeRegDefTbl( Rno )) == NULL )
		{
			Hmsg->ErrRno = (USHORT)RegNo;					/* 異常レジスタ番号						*/
			return( REGIF_REGNOERR );						/* レジスタ番号異常(REG番号無し)		*/
		}
/*--------------------------------------------------------------------------------------------------*/
/*		開始レジスタ整合チェック																	*/
/*--------------------------------------------------------------------------------------------------*/
		if( OpeRegTbl[0].Attr.BgnData == 0 )
		{
			return( REGIF_DAMTHERR );						/* データ整合異常(LONG境界)				*/
		}

/*--------------------------------------------------------------------------------------------------*/
/*		レジスタデータ読出し																		*/
/*--------------------------------------------------------------------------------------------------*/
		for( i=0; i < Number; i++ )
		{
			if( OpeRegTbl[i].VarAdr == NULL )
			{
				Hmsg->ErrRno = (USHORT)(RegNo + i);			/* 異常レジスタ番号						*/
				return( REGIF_REGNOERR );					/* レジスタ番号異常(REG範囲超過)		*/
			}
			if( OpeRegTbl[i].Rno != (Rno + i) )
			{
				Hmsg->ErrRno = (USHORT)(RegNo + i);			/* 異常レジスタ番号						*/
				return( REGIF_REGNOERR );					/* レジスタ番号異常(REG番号非連続)		*/
			}
			if( OpeRegTbl[i].Attr.ReadEnable == 0 )
			{
				Hmsg->ErrRno = (USHORT)(RegNo + i);			/* 異常レジスタ番号						*/
				return( REGIF_REGNOERR );					/* レジスタ番号異常(READ不可属性)		*/
			}

			if( OpeRegTbl[i].ReadRegFunc != NULL )													  /*<V717>Add:Start*/
			{
				rc = OpeRegTbl[i].ReadRegFunc( &OpeRegTbl[i], &RdBuf[i], Hmsg );
				if ( rc != REGIF_COMPLETE ) { return rc; }
			}
			else if( OpeRegTbl[i].Attr.IsLong == 1 )												  /*<V717>Add:End  */
			{
			    OpRegData = (*((volatile uint16_t *)((uintptr_t)(OpeRegTbl[i].VarAdr))));
                *(USHORT *)&RdBuf[4 * i + 1] = (OpRegData )  & 0x00FF;
                *(USHORT *)&RdBuf[4 * i + 0] = (OpRegData >> 8)  & 0x00FF;
                OpRegData = (*((volatile uint16_t *)((uintptr_t)(OpeRegTbl[i].VarAdr + 1))));
                *(USHORT *)&RdBuf[4 * i + 3] = OpRegData & 0x00FF;
                *(USHORT *)&RdBuf[4 * i + 2] = (OpRegData >> 8) & 0x00FF;

                i++;							  /* <V760> */
			}
			else
			{
			    *(USHORT *)&RdBuf[2 * i + 1] =  (*(USHORT*)OpeRegTbl[i].VarAdr)  & 0x00FF;
			    *(USHORT *)&RdBuf[2 * i + 0] = ((*(USHORT*)OpeRegTbl[i].VarAdr) >> 8) & 0x00FF;
			}
		}
/*--------------------------------------------------------------------------------------------------*/
/*		終了レジスタ整合チェック																	*/
/*--------------------------------------------------------------------------------------------------*/
		if( OpeRegTbl[Number-1].Attr.EndData == 0 )
		{
			return( REGIF_DAMTHERR );						/* データ整合異常(LONG境界)				*/
		}
/*--------------------------------------------------------------------------------------------------*/
		return( REGIF_COMPLETE );
}

/****************************************************************************************************/
/*																									*/
/*		操作寄存器写入																				*/
/*																									*/
/****************************************************************************************************/
LONG	RpxWriteOpeRegister( LONG RegNo, LONG Number, USHORT *WrBuf, HMSGIF Hmsg )
{
LONG	rc = 0;

/*--------------------------------------------------------------------------------------------------*/
/*		写入权限检查																				*/
/*--------------------------------------------------------------------------------------------------*/
		if( RpxGetOpeRegWriteHandle( Hmsg ) != TRUE )
		{
			return( REGIF_FOPEERR1 );						/* 当前操作处理占用 ,禁止操作   		*/
		}
/*--------------------------------------------------------------------------------------------------*/
/*		数据跟踪终了检查																			*/
/*--------------------------------------------------------------------------------------------------*/
		LpxCheckDataTraceEnd( Hmsg );						/* 数据采集完成检查処理          		*/

/*--------------------------------------------------------------------------------------------------*/
/*		写入寄存器番号处理																			*/
/*--------------------------------------------------------------------------------------------------*/
		switch( RegNo & 0x0FFF )
		{
            /*--------------------------------------------------------------------------------------*/
            case 0x0100: /* 操作模式设定(进入or退出辅助功能功能,数据采集(单次,连续)					*/
            /*--------------------------------------------------------------------------------------*/
                if( Number != 1 )
                {
                    rc = REGIF_DAMTHERR;					/* 数据异常(不符合相应的指令格式)       */
                }
                else
                {
                    rc = LpxWriteOpeModeRegister( WrBuf[0], Hmsg );
                }
                break;
            /*--------------------------------------------------------------------------------------*/
            case 0x0101: /* 操作命令																*/
            /*--------------------------------------------------------------------------------------*/
                if( Number != 1 )
                {
                    rc = REGIF_DAMTHERR;						/* 数据异常(不符合相应的指令格式)   */
                }
                else
                {
                    rc = LpxWriteOpeCmdRegister( WrBuf[0], Hmsg );
                }
                break;
            /*--------------------------------------------------------------------------------------*/
            case 0x0102: /* 操作終了																*/
            /*--------------------------------------------------------------------------------------*/
                if( Number != 1 )
                {
                    rc = REGIF_DAMTHERR;						/* 数据异常(不符合相应的指令格式)   */
                }
                else
                {
                    rc = LpxWriteOpeEndRegister( WrBuf[0], Hmsg );
                }
                break;
            /*--------------------------------------------------------------------------------------*/
            case 0x0103: /*             	    													*/
            /*--------------------------------------------------------------------------------------*/
                if( Number != 1 )
                {
                    rc = REGIF_DAMTHERR;						/* 数据异常(不符合相应的指令格式)   */
                }
                else
                {
                    rc = LpxWriteOpeEtcRegister( RegNo, Number, WrBuf, Hmsg );
                }
                break;
            /*--------------------------------------------------------------------------------------*/
            case 0x0104: /* 第2操作模式设定															*/
            /*--------------------------------------------------------------------------------------*/
                if( Number != 1 )
                {
                    rc = REGIF_DAMTHERR;						/* 数据异常(不符合相应的指令格式)   */
                }
                else
                {
                    rc = LpxWriteOpeMode2Register( WrBuf[0], Hmsg );
                }
                break;
            /*--------------------------------------------------------------------------------------*/
            case 0x0105: /* 第2操作命令，并进行命令代码转换											*/
            /*--------------------------------------------------------------------------------------*/
                if( Number != 1 )
                {
                    rc = REGIF_DAMTHERR;						/* 数据异常(不符合相应的指令格式)   */
                }
                else
                {
                    rc = LpxWriteOpeCmd2Register( WrBuf[0], Hmsg );
                }
                break;
            /*--------------------------------------------------------------------------------------*/
            case 0x0106: /* 跟踪操作模式设定														*/
            /*--------------------------------------------------------------------------------------*/
                 if( Number != 1 )
                 {
                    rc = REGIF_DAMTHERR;						/* 数据异常(不符合相应的指令格式)   */
                 }
                 else
                 {
                    rc = LpxWriteOpeModeTrcRegister( WrBuf[0], Hmsg );
                 }
                 break;
            /*--------------------------------------------------------------------------------------*/
            case 0x0107: /* 跟踪操作指令															*/
            /*--------------------------------------------------------------------------------------*/
                 if( Number != 1 )
                 {
                    rc = REGIF_DAMTHERR;						/* 数据异常(不符合相应的指令格式)   */
                 }
                 else
                 {
                    rc = LpxWriteOpeCmdTrcRegister( WrBuf[0], Hmsg );
                 }
                 break;
            /*--------------------------------------------------------------------------------------*/
            default:     /* 其他寄存器写入															*/
            /*--------------------------------------------------------------------------------------*/
                rc = LpxWriteOpeEtcRegister( RegNo, Number, WrBuf, Hmsg );
                break;
		}
/*--------------------------------------------------------------------------------------------------*/
/*		Check return Code																			*/
/*--------------------------------------------------------------------------------------------------*/
		if ( (rc != REGIF_COMPLETE) && (rc != REGIF_CONTINUE) )
		{
			LpxFreeOpeRegWriteHandle( Hmsg );
		}
/*--------------------------------------------------------------------------------------------------*/
		return( rc );

}




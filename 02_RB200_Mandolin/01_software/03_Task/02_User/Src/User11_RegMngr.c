/****************************************************************************************************/
/*																									*/
/*		寄存器数据的上下限� �         																*/
/*																									*/
/****************************************************************************************************/
LONG	LpxCheckRegDataLimit( USHORT *WrBuf, CFORDEF *OpeRegTbl )
{
LONG	x;
ULONG	y;
/*--------------------------------------------------------------------------------------------------*/
/*		Byte Data Limit Check																		*/
/*--------------------------------------------------------------------------------------------------*/
		if( OpeRegTbl[0].Attr.IsDbyte )
		{
			if( OpeRegTbl[0].Attr.WithSign )				/* Signed Char							*/
			{
				x = (CHAR)(WrBuf[0] & 0xFF);
				if( (x < OpeRegTbl[0].Llimit.Char[0]) || (x > OpeRegTbl[0].Ulimit.Char[0]) )
				{
					return( FALSE );                        /* 寄存器上 限異常 					*/
				}
				x = (CHAR)((WrBuf[0] >> 8) & 0xFF);
				if( (x < OpeRegTbl[0].Llimit.Char[1]) || (x > OpeRegTbl[0].Ulimit.Char[1]) )
				{
					return( FALSE );
				}
			}
			else                                            /* Unsigned Char						*/
			{
				y = (UCHAR)(WrBuf[0] & 0xFF);
				if( (y < OpeRegTbl[0].Llimit.Uchar[0]) || (y > OpeRegTbl[0].Ulimit.Uchar[0]) )
				{
					return( FALSE );
				}
				y = (UCHAR)((WrBuf[0] >> 8) & 0xFF);
				if( (y < OpeRegTbl[0].Llimit.Uchar[1]) || (y > OpeRegTbl[0].Ulimit.Uchar[1]) )
				{
					return( FALSE );
				}
			}
		}
/*--------------------------------------------------------------------------------------------------*/
/*		Short Data Limit Check																		*/
/*--------------------------------------------------------------------------------------------------*/
		else if( OpeRegTbl[0].Attr.IsLong == 0 )
		{
			if( OpeRegTbl[0].Attr.WithSign )				/* Signed Short							*/
			{
				x = (SHORT)WrBuf[0];
				if( (x < OpeRegTbl[0].Llimit.Short) || (x > OpeRegTbl[0].Ulimit.Short) )
				{
					return( FALSE );
				}
			}
			else											/* Unsigned Short						*/
			{
				y = (USHORT)WrBuf[0];
				if( (y < OpeRegTbl[0].Llimit.Ushort) || (y > OpeRegTbl[0].Ulimit.Ushort) )
				{
					return( FALSE );
				}
			}
		}
/*--------------------------------------------------------------------------------------------------*/
/*		Long Data Limit Check																		*/
/*--------------------------------------------------------------------------------------------------*/
		else if( OpeRegTbl[0].Attr.IsLong && OpeRegTbl[0].Attr.BgnData )
		{
			if( OpeRegTbl[0].Attr.WithSign )
			{
				x = LONGOF( WrBuf[0], WrBuf[1] );
				if( (x < LONGOF( OpeRegTbl[0].Llimit.w, OpeRegTbl[1].Llimit.w ))
				||	(x > LONGOF( OpeRegTbl[0].Ulimit.w, OpeRegTbl[1].Ulimit.w ))	)
				{
					return( FALSE );
				}
			}
			else
			{
				y = ULONGOF( WrBuf[0], WrBuf[1] );
				if( (y < ULONGOF( OpeRegTbl[0].Llimit.w, OpeRegTbl[1].Llimit.w ))
				||	(y > ULONGOF( OpeRegTbl[0].Ulimit.w, OpeRegTbl[1].Ulimit.w ))	)
				{
					return( FALSE );
				}
			}
		}
/*--------------------------------------------------------------------------------------------------*/        
		return( TRUE );

}




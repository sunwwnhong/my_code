/*																									*/
/*		レジスタＩＦ管理初期化処理																	*/
/*																									*/
/****************************************************************************************************/
void	RpxInitRegManager( void )
{
LONG	i;
LONG	EntNum;

/*--------------------------------------------------------------------------------------------------*/
/*		寄存器ＩＦ管理用変数初期化処理															    */
/*--------------------------------------------------------------------------------------------------*/
        MlibResetSHORTMemory( &RifMngV, sizeof(RifMngV) );
		RifMngV.Hmsg = NULL;
/*--------------------------------------------------------------------------------------------------*/
/*		寄存器ＩＦ管理用的变量計算処理														        */
/*--------------------------------------------------------------------------------------------------*/
		EntNum = FUNOPEREGDEFTBL_ENTNUM;
		RifMngP.FunOpeRegDefTblEntNum = EntNum;
		RifMngP.FunOpeRegDefTblSrhIniW = (1UL << MlibSrhbiton( EntNum - 1, SRH_FROM_MSB ));

/*--------------------------------------------------------------------------------------------------*/
/*		辅助功能操作寄存器的初始化处理																*/
/*--------------------------------------------------------------------------------------------------*/
		for( i=0; i < EntNum; i++ )
		{
			if( FunOpeRegDefTbl[i].VarAdr != NULL )
			{
				*(USHORT*)FunOpeRegDefTbl[i].VarAdr = FunOpeRegDefTbl[i].IniVal;
			}
		}

/*--------------------------------------------------------------------------------------------------*/
/*		補助機能アプリ初期化関数実行処理															*/
/*--------------------------------------------------------------------------------------------------*/
		RifMngP.FnFunTblEntNum = FNFUNTBL_ENTNUM;
		for( i=0; i < RifMngP.FnFunTblEntNum; i++ )
		{
			if( FnFunTbl[i].FunDefPtr->FuncInitPtr != NULL )
			{
				FnFunTbl[i].FunDefPtr->FuncInitPtr( );
			}
		}
/*--------------------------------------------------------------------------------------------------*/
		return;
}



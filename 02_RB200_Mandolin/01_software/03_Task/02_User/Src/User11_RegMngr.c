/****************************************************************************************************/
/*																									*/
/*		操作モード操作结束处理																		*/
/*																									*/
/****************************************************************************************************/
LONG	LpxFinishOpeModeOperation( USHORT FinishAll, USHORT FinishExeIdx, HMSGIF Hmsg )
{
LONG	rc;

/*--------------------------------------------------------------------------------------------------*/
/*		操作结束处理１																				*/
/*--------------------------------------------------------------------------------------------------*/
		if( FinishAll || (FinishExeIdx == 0) )
		{
			rc = LpxFinishDataTrace( OpeReg.OpeMode );		/* データトレース停止処理				*/
            /*--------------------------------------------------------------------------------------*/
			if( rc == FALSE )
			{
				RpxFunFinishReg( 0, 0, Hmsg );				/* 補助機能実行结束处理					*/
			}
            /*--------------------------------------------------------------------------------------*/
			OpeReg.OpeMode = 0x0000;						/* リセット操作モード					*/
			OpeReg.OpeCmd  = 0x0000;						/* リセット操作コマンド					*/
		}
/*--------------------------------------------------------------------------------------------------*/
/*		操作结束处理２																				*/
/*--------------------------------------------------------------------------------------------------*/
		if( FinishAll || (FinishExeIdx == 1) )
		{
			RpxFunFinishReg( 0, 1, Hmsg );					/* 補助機能実行结束处理					*/
            /*--------------------------------------------------------------------------------------*/
			OpeReg.OpeMode2 = 0x0000;						/* リセット操作モード					*/
			OpeReg.OpeCmd2  = 0x0000;						/* リセット操作コマンド					*/
		}
/*--------------------------------------------------------------------------------------------------*/
/*		操作结束处理(DataTrace)																		*/
/*--------------------------------------------------------------------------------------------------*/
		if( FinishAll || (FinishExeIdx == 2) )
		{
			LpxFinishDataTrace( OpeReg.OpeModeTrc );		/* データトレース停止処理				*/
            /*--------------------------------------------------------------------------------------*/
			OpeReg.OpeModeTrc = 0x0000;						/* リセット操作モード					*/
			OpeReg.OpeCmdTrc  = 0x0000;						/* リセット操作コマンド					*/
		}
/*--------------------------------------------------------------------------------------------------*/
/*		操作レジスタ書込み権解放																	*/
/*--------------------------------------------------------------------------------------------------*/
		LpxFreeOpeRegWriteHandle( Hmsg );
/*--------------------------------------------------------------------------------------------------*/
		return( REGIF_COMPLETE );

}





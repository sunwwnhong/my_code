/*																									*/
/*		第２操作モードレジスタ書込み処理															*/
/*																									*/
/****************************************************************************************************/
LONG	LpxWriteOpeMode2Register( USHORT WrData, HMSGIF Hmsg )
{
LONG	rc;

/*--------------------------------------------------------------------------------------------------*/
/*		通常モード設定																				*/
/*--------------------------------------------------------------------------------------------------*/
		if( WrData == 0 )
		{
			LpxFinishOpeModeOperation( 0, 1, Hmsg );		/* 操作モード操作終了処理				*/
			return( REGIF_COMPLETE );
		}
/*--------------------------------------------------------------------------------------------------*/
/*		操作モードチェック																			*/
/*--------------------------------------------------------------------------------------------------*/
		else if( OpeReg.OpeMode2 != 0 )
		{
			return( REGIF_DAMTHERR );
		}
/*--------------------------------------------------------------------------------------------------*/
/*		第１操作モードチェック																		*/
/*--------------------------------------------------------------------------------------------------*/
		else if( (OpeReg.OpeMode == OPEMODE_DATATRACE) || (OpeReg.OpeMode == OPEMODE_RINGTRACE) )
		{
			return( REGIF_PARARUNERR2 );
		}
/*--------------------------------------------------------------------------------------------------*/
/*		補助機能開始処理																			*/
/*--------------------------------------------------------------------------------------------------*/
		else
		{
			rc = RpxFunExeBeginReg( WrData, 1, OpeReg.OpeModeTrc, Hmsg );
			if( (rc == REGIF_COMPLETE) || (rc == REGIF_CONTINUE) ){ OpeReg.OpeMode2 = WrData;}
			return( rc );
		}

}





          
/****************************************************************************************************/
/*																									*/
/*		操作指令レジスタ書込み処理																	*/
/*																									*/
/****************************************************************************************************/
LONG	LpxWriteOpeCmdRegister( USHORT WrData, HMSGIF Hmsg )
{
LONG	rc;
/*--------------------------------------------------------------------------------------------------*/
/*		在没有进入数据采集(单次,连续),� δ苁�,直接退出处理   									*/
/*--------------------------------------------------------------------------------------------------*/
		if( OpeReg.OpeMode == 0 )
		{
			return( REGIF_DAMTHERR );
		}
/*--------------------------------------------------------------------------------------------------*/
/*		数据采集：� �     																		    */
/*--------------------------------------------------------------------------------------------------*/
		else if( OpeReg.OpeMode == OPEMODE_DATATRACE )
		{
            rc = LpxSetDataTraceCmd( OpeReg.OpeMode, WrData );
			if( rc == REGIF_COMPLETE )
            {
                OpeReg.OpeCmd = WrData;
            }
			return( rc );
		}
/*--------------------------------------------------------------------------------------------------*/
/*		数据采集：连续        																		*/
/*--------------------------------------------------------------------------------------------------*/
		else if( OpeReg.OpeMode == OPEMODE_RINGTRACE )
		{
			rc = LpxSetDataTraceCmd( OpeReg.OpeMode, WrData );
			if( rc == REGIF_COMPLETE )
            {
                OpeReg.OpeCmd = WrData;
            }
			return( rc );
		}
/*--------------------------------------------------------------------------------------------------*/
/*		辅助功能       																			*/
/*--------------------------------------------------------------------------------------------------*/
		else
		{
			rc = RpxFunExeReg( WrData, 0, Hmsg );       /* 写入到寄存器*/
			if( (rc == REGIF_COMPLETE) || (rc == REGIF_CONTINUE) ){ OpeReg.OpeCmd = WrData;}
			return( rc );
		}

}





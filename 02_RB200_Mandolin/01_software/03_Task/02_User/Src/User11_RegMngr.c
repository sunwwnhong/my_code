/****************************************************************************************************/
/*																									*/
/*		一般寄存器写入� 鲃I理     																	*/
/*																									*/
/****************************************************************************************************/
LONG	LpxWriteOpeEtcRegister( LONG RegNo, LONG Number, USHORT *WrBuf, HMSGIF Hmsg )
{
LONG	i, rc;
void	*pErrVar;
CFORDEF	*OpeRegTbl;

LONG	Rno = RegNo & 0xFFF;
        /* 判断寄存器是否存在,并将OpeRegTbl指向检索到的功能码的指针 */
		if( (OpeRegTbl = LpxSearchFunOpeRegDefTbl( Rno )) == NULL )
		{
			Hmsg->ErrRno = (USHORT)RegNo;
			return( REGIF_REGNOERR );
		}
/*--------------------------------------------------------------------------------------------------*/
/*		对寄存器的完整性进行检查(即判断是否为32位数据的高16位,如果是则报警)							*/
/*--------------------------------------------------------------------------------------------------*/
		if( OpeRegTbl[0].Attr.BgnData == 0 )
		{
			return( REGIF_DAMTHERR );                       /* データ整合異常(LONG State)			*/
		}
/*--------------------------------------------------------------------------------------------------*/
/*		寄存器号,地址,写入属性,上下限 Check															*/
/*--------------------------------------------------------------------------------------------------*/
		for( i=0; i < Number; i++ )
		{
            /*--------------------------------------------------------------------------------------*/
			if( OpeRegTbl[i].VarAdr == NULL )               /* 判断此功能号对应的地址是否为NULL     */
			{
				Hmsg->ErrRno = (USHORT)(RegNo + i);         /* 異常レジスタ番号						*/
				return( REGIF_REGNOERR );                   /* レジスタ番号異常(REG範囲超過)		*/
			}
            /*--------------------------------------------------------------------------------------*/
			if( OpeRegTbl[i].Rno != (Rno + i) )             /*  再一次检查Rno                       */
			{
				Hmsg->ErrRno = (USHORT)(RegNo + i);
				return( REGIF_REGNOERR );                   /* レジスタ番号異常(REG番号非连续)		*/
			}
            /*--------------------------------------------------------------------------------------*/
			if( OpeRegTbl[i].Attr.WriteEnable == 0 )        /* 判断是否可写入                       */
			{
				Hmsg->ErrRno = (USHORT)(RegNo + i);
				return( REGIF_REGNOERR );                   /* レジスタ番号異常(WRITE不可属性)		*/
			}
            /*--------------------------------------------------------------------------------------*/
			if( LpxCheckRegDataLimit( &WrBuf[i], &OpeRegTbl[i] ) == FALSE )
			{
				Hmsg->ErrRno = (USHORT)(RegNo + i);
				return( REGIF_DALMTERR );                   /* データ上下限检验異常				    */
			}
            /*--------------------------------------------------------------------------------------*/
		}
/*--------------------------------------------------------------------------------------------------*/
/*		寄存器完整性检查(低16位 or 高16位)														    */
/*--------------------------------------------------------------------------------------------------*/
		if( OpeRegTbl[Number-1].Attr.EndData == 0 )
		{
			return( REGIF_DAMTHERR );                       /* データ整合異常(LONG境界)				*/
		}
/*--------------------------------------------------------------------------------------------------*/
/*		レジスタデータ書込み																		*/
/*--------------------------------------------------------------------------------------------------*/
		for( i=0; i < Number; i++ )
		{
			if( OpeRegTbl[i].WriteRegFunc != NULL )         /* 写入到寄存器的功能码*/
			{
				if( OpeRegTbl[i].Attr.IsLong == 0 )         /* 16位数据 */
				{
					rc = OpeRegTbl[i].WriteRegFunc( &OpeRegTbl[i], WrBuf[i], Hmsg );
				}
				else if( OpeRegTbl[i].Attr.BgnData )        /* 32位数据 */
				{
					rc = OpeRegTbl[i].WriteRegFunc( &OpeRegTbl[i], ULONGOF(WrBuf[i],WrBuf[i+1]), Hmsg );
					if( rc == REGWRF_COMPLETE ){ i++;}
				}
				else
				{
					continue;                               /* 下一个功能码*/
				}
            /*--------------------------------------------------------------------------------------*/
				if( rc != REGWRF_COMPLETE )
				{
					Hmsg->ErrRno = (USHORT)(RegNo + i);
					if( rc == REGWRF_DATAERROR    ){ return( REGIF_DAMTHERR     );}
					if( rc == REGWRF_WRITEFAIL    ){ return( REGIF_WRITEFAIL    );}
					if( rc == REGWRF_WRITEDISABLE ){ return( REGIF_WRITEDISABLE );}
#if (CCSW_SFTID == SFTID_EXIST)
					if( rc == REGWRF_ACCESSERROR  ){ return( REGIF_REGNOERR     );}
					if( rc == REGWRF_DATRNGERROR  ){ return( REGIF_DALMTERR     );}
#endif
					return( REGIF_DAMTHERR );
				}
			}
        /*--------------------------------------------------------------------------------------*/
			else if( OpeRegTbl[i].Attr.IsLong == 0 )
			{
				*(USHORT*)(OpeRegTbl[i].VarAdr) = WrBuf[i];
			}
			else if( OpeRegTbl[i].Attr.BgnData )
			{
				*(ULONG*)(OpeRegTbl[i].VarAdr) = ULONGOF( WrBuf[i], WrBuf[i+1] ); i++;
			}
		}
/*--------------------------------------------------------------------------------------------------*/
/*		数据跟踪/Table运转参数计算																	*/
/*--------------------------------------------------------------------------------------------------*/
        pErrVar = RpxWriteTrcTdrvOpeRegEnd( );
		if( pErrVar != NULL )
		{
			Hmsg->ErrRno = LpxSearchErrorOpeRegNo( pErrVar, RegNo );
			if ( Hmsg->ErrRno == REG_TRC_USESIZE )
			{
				return ( REGIF_DAMTHERR2 );			    /* データ整合異常(条件エラー)				*/
			}
			else
			{
				return( REGIF_DACALERR );				/* データ異常(演算異常)						*/
			}
		}
/*--------------------------------------------------------------------------------------------------*/
/*		释放写入权限																				*/
/*--------------------------------------------------------------------------------------------------*/        
		LpxFreeOpeRegWriteHandle( Hmsg );

		return( REGIF_COMPLETE );

}





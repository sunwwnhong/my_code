/****************************************************************************************************/
/*																									*/
/*		操作状态写入处理           																*/
/*																									*/
/****************************************************************************************************/
LONG	LpxWriteOpeModeRegister( USHORT WrData, HMSGIF Hmsg )
{
LONG	rc;
/*--------------------------------------------------------------------------------------------------*/
/*		普通模式设定(退出处理)																		*/
/*--------------------------------------------------------------------------------------------------*/
		if( WrData == 0 )
		{
			LpxFinishOpeModeOperation( 0, 0, Hmsg );            /* 操作模式结束 理     			*/
			return( REGIF_COMPLETE );
		}
/*--------------------------------------------------------------------------------------------------*/
/*		操作模式检查																				*/
/*--------------------------------------------------------------------------------------------------*/        
		else if( OpeReg.OpeMode != 0 )                         /* 已经进入了辅助功能模式后再发送进入*/
		{                                                      /* 辅助功能模式,则返回相应的故障信息 */
			return( REGIF_DAMTHERR );
		}
/*--------------------------------------------------------------------------------------------------*/
/*		跟踪模式设定：单发																			*/
/*--------------------------------------------------------------------------------------------------*/        
		else if( WrData == OPEMODE_DATATRACE )      //0x2000
		{
            /*--------------------------------------------------------------------------------------*/
			if( OpeReg.OpeMode2 != 0 )
            {
                return( REGIF_PARARUNERR1 );
            }
            /*--------------------------------------------------------------------------------------*/
			rc = LpxSetDataTraceMode( OpeReg.OpeModeTrc, WrData, Hmsg );
			if( rc == REGIF_COMPLETE )
            {
                OpeReg.OpeMode = WrData;
            }
			return( rc );
		}
/*--------------------------------------------------------------------------------------------------*/
/*		跟踪模式设定：连续																			*/
/*--------------------------------------------------------------------------------------------------*/
		else if( WrData == OPEMODE_RINGTRACE )     //0x200A
		{
            /*--------------------------------------------------------------------------------------*/
			if( OpeReg.OpeMode2 != 0 )
            {
                return( REGIF_PARARUNERR1 );
            }
            /*--------------------------------------------------------------------------------------*/
			rc = LpxSetDataTraceMode( OpeReg.OpeModeTrc, WrData, Hmsg );
            /*--------------------------------------------------------------------------------------*/
			if( rc == REGIF_COMPLETE ){ OpeReg.OpeMode = WrData;}
			return( rc );
		}
/*--------------------------------------------------------------------------------------------------*/
/*		辅助功能开始处理																			*/
/*--------------------------------------------------------------------------------------------------*/
		else
		{
			rc = RpxFunExeBeginReg( WrData, 0, OpeReg.OpeModeTrc, Hmsg );
			if( (rc == REGIF_COMPLETE) || (rc == REGIF_CONTINUE) )
            {
                OpeReg.OpeMode = WrData;
            }
			return( rc );
		}

}


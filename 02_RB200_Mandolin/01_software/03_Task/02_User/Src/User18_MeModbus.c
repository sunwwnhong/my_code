/**********************************************************************************************/
/* Function_Name: MemobusRespTrnsStart         */
/* Description  : 响应数据传输设置
                  调用：Modbus数据响应时调用
                  所有发送数据准备好后，都会进入这个函数执行状态改变，进而进入发送状态。
                  输入：
                  输出：   */
/**********************************************************************************************/
void	MemobusRespTrnsStart( HMSGIF Hmsg, USHORT Size, USHORT Status )
{
UCHAR	Channel;

        /*------------------------------------------------------------------------------------------*/
        /*		数据发送通道设置，串口或USB												            */
        /*------------------------------------------------------------------------------------------*/
		if ( Hmsg == &SrlMsg )
		{
			Channel = COM0_UART;
		}
        /*------------------------------------------------------------------------------------------*/
		else if ( Hmsg == &UsbMsg )
		{
			Channel = COM1_USB;
		}
        /*------------------------------------------------------------------------------------------*/
		else
		{
			return;
		}
        /*------------------------------------------------------------------------------------------*/
        /*		响应数据传输指令																	        */
        /*------------------------------------------------------------------------------------------*/
		DriComSetTrnsStart( Channel, Size, Status );

		return;
}

/**********************************************************************************************/
/* Function_Name: RpiWordSwap         */
/* Description  : 两个字节合成一个字
                  调用：根据需要调用
                  输入：高字节地址
                  输出：合成后的字  */
/**********************************************************************************************/
USHORT	RpiWordSwap( void *pStream  )
{
	return ( (USHORT)((*(UCHAR *)pStream << 8) + *((UCHAR *)pStream + 1)) );
}



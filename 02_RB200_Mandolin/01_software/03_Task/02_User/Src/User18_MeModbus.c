#define	SMBUS_RESSIZ_ERR	    5				    /* 异常应答消息长度								*/

/*--------------------------------------------------------------------------------------------------*/
#define	SMBUS_EC_FNCDERR		(0x01 | 0x200)		/* 函数代码异常									*/
#define	SMBUS_EC_REGNOERR		(0x02 | 0x200)		/* 寄存器号异常   								*/
#define	SMBUS_EC_DANUMERR		(0x03 | 0x200)		/* 個数不良異常									*/
/*--------------------------------------------------------------------------------------------------*/
#define	SMBUS_SEC_REGNOERR		(0x30 | 0x200)		/* 功能码不存在              				    */
#define	SMBUS_SEC_ACCSERR		(0x31 | 0x200)		/* 访问权限限制的错误							*/
#define	SMBUS_SEC_DALMTERR		(0x32 | 0x200)		/* 设定值范围溢出  							    */
#define	SMBUS_SEC_DAMTHERR		(0x33 | 0x200)		/* 数据匹配错误       							*/
#define	SMBUS_SEC_CONDERR		(0x34 | 0x200)		/* 条件 误 									    */

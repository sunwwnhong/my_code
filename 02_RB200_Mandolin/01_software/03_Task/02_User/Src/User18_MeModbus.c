/**********************************************************************************************/
/* Function_Name: RpxSmsgFuncExecResponse         */
/* Description  : 辅助功能执行结果响应处理
                  调用：辅助功能执行结果响应-函数中调用
                  输入：
                  输出：   */
/**********************************************************************************************/
void	RpxSmsgFuncExecResponse( HMSGIF Hmsg )
{
    USHORT	*CmdBufW = (USHORT *)(Hmsg->CmdBuf);  // 获取接收缓存指针
    USHORT	*ResBufW = (USHORT *)(Hmsg->ResBuf);  // 获取发送缓存指针
    USHORT	TrnsLen;

        /*** 若协议执行状态不为完成或继续，返回错误帧 ***/
		if ( (Hmsg->FunSts != REGIF_COMPLETE) && (Hmsg->FunSts != REGIF_CONTINUE) )
		{
			MemobusSendErrorResponse( Hmsg, Hmsg->FunSts );
			return;
		}
        /*** 若协议执行状态为完成或继续，正常返回接收数据帧 ***/
		ResBufW[0] = CmdBufW[0];
		ResBufW[1] = CmdBufW[1];
		ResBufW[2] = CmdBufW[2];
		TrnsLen = 8;

		MlibSetCRC16( Hmsg->ResBuf, TrnsLen );  // 进行CRC计算
		MemobusRespTrnsStart( Hmsg, TrnsLen, RESMT_COMPLETE );   // 响应数据传输设置
		return;
}

/**********************************************************************************************/
/* Function_Name: MemobusSendErrorResponse         */
/* Description  : modbus异常应答回复处理
                  调用：检测到通讯异常，调用此函数应答
                  输入：
                  输出：   */
/**********************************************************************************************/
void	MemobusSendErrorResponse( HMSGIF Hmsg, ULONG ErrCode )
{
UCHAR	*CmdBuf = (UCHAR *)(Hmsg->CmdBuf);
UCHAR	*ResBuf = (UCHAR *)(Hmsg->ResBuf);
USHORT	MbusErrCode;

/*--------------------------------------------------------------------------------------------------*/
/*		 生成异常响应数据 */
/*--------------------------------------------------------------------------------------------------*/
		switch ( (USHORT)ErrCode )
		{
            /*--------------------------------------------------------------------------------------*/
			case	REGIF_CMDERR	:			/* 取的功能码地址异常(不存在)			        */
				MbusErrCode = SMBUS_EC_FNCDERR;
				break;
			/*--------------------------------------------------------------------------------------*/	
			case	REGIF_REGNOERR	:			/* 寄存器编号/地址异常                   			    */
				MbusErrCode = SMBUS_SEC_ACCSERR;
				break;
			/*--------------------------------------------------------------------------------------*/	
			case	REGIF_DALMTERR	:			/* 数据异常 						        */
				MbusErrCode = SMBUS_SEC_DALMTERR;
				break;
			/*--------------------------------------------------------------------------------------*/	
			case	REGIF_DACALERR	:			/* 数据异常(计算异常) 							        */
			case	REGIF_DAMTHERR	:			/* 数据完整性异常								        */
				MbusErrCode = SMBUS_SEC_DAMTHERR;
				break;
			/*--------------------------------------------------------------------------------------*/	
			case	REGIF_DAMTHERR2	:			/* 数据完整性异常 								        */
				MbusErrCode = SMBUS_SEC_CONDERR;
				break;
			/*--------------------------------------------------------------------------------------*/	
			case	REGIF_DANUMERR	:			/* 数据个数异常   								        */
				MbusErrCode = SMBUS_EC_DANUMERR;
				break;
			/*--------------------------------------------------------------------------------------*/	
			case	REGIF_FOPEERR1	:
			case	REGIF_FOPEERR2	:			/* 不可处理(由其他信道产生操作模式占用)			        */
			case	REGIF_FOPEERR3	:
				MbusErrCode = SMBUS_SEC_INTLOCKERR;
				break;
			/*--------------------------------------------------------------------------------------*/	
			default	:
				MbusErrCode = (USHORT)ErrCode;
				break;
            /*--------------------------------------------------------------------------------------*/
		}

/*--------------------------------------------------------------------------------------------------*/
/*		 生成异常响应数据 */
/*--------------------------------------------------------------------------------------------------*/
		ResBuf[0] = CmdBuf[0];						/* AxisAddr										*/
		ResBuf[1] = CmdBuf[1] | 0x80;				/* Function Code								*/
		ResBuf[2] = (UCHAR)(MbusErrCode & 0xFF);	/* Error Code									*/
/*--------------------------------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------------------------------*/
/*		CRC计算，异常应答信息长度5																					    */
/*--------------------------------------------------------------------------------------------------*/
		MlibSetCRC16( Hmsg->ResBuf, SMBUS_RESSIZ_ERR );

/*--------------------------------------------------------------------------------------------------*/
/*		应答数据发送指令																			    */
/*--------------------------------------------------------------------------------------------------*/
		MemobusRespTrnsStart( Hmsg, SMBUS_RESSIZ_ERR, RESMT_COMPLETE );
		return;
}



/**********************************************************************************************/
/* Function_Name: ARM_IC_Response         */
/* Description  : 读取IC信息-ARM+FPGA芯片型号，并生成应答数据
                  调用：modbus读取寄存器读取地址0xBFA0情况下，执行
                  这里用来返回arm和FPGA功能的类型号，用来作为机型硬件类型的判断
                  输入：
                  输出：   */
/**********************************************************************************************/
void	ARM_IC_Response( HMSGIF Hmsg )
{
UCHAR	*CmdBuf = (UCHAR *)(Hmsg->CmdBuf);
UCHAR	*ResBuf = (UCHAR *)(Hmsg->ResBuf);


/*--------------------------------------------------------------------------------------------------*/
/*		生成异常响应数据																			    */
/*--------------------------------------------------------------------------------------------------*/
		ResBuf[0] = CmdBuf[0];						/* AxisAddr										*/
		ResBuf[1] = CmdBuf[1];				        /* Function Code								*/
		ResBuf[2] = 0x02;
        ResBuf[3] = 0x26;       // ARM-----0x20: HDSC 4A0
                                //         0x21: ST H723
                                //         0x22: ST F745
                                // DSP-----0x23:28385D(SD100)
        						// 		   0x24:28385D(RB100)
        						// 		   0x25:28385D(RB300)
        						// 		   0x26:28385D(RB200)
        						// 		   0x27:28385D(SD200)
        ResBuf[4] = 0x30;       //  0x03: (A型机)

        if( Kprm.f.StandardType )
        {
            ResBuf[4] |= 0x01;      // 脉冲机型
        }
        else if( Kprm.f.CanOpenNet )
        {
            ResBuf[4] |= 0x02;      // CanOpen 机型
        }
        else if( Kprm.f.EtherCATNet)
        {
            ResBuf[4] |= 0x03;      // EtherCAT 机型
        }
        else
        {
            ResBuf[4] |= 0x04;      // PN 或其它机型
        }
/*--------------------------------------------------------------------------------------------------*/

/*--------------------------------------------------------------------------------------------------*/
/*		CRC计算																					    */
/*--------------------------------------------------------------------------------------------------*/
		MlibSetCRC16( Hmsg->ResBuf, 7 );

/*--------------------------------------------------------------------------------------------------*/
/*		响应数据传输设置																			    */
/*--------------------------------------------------------------------------------------------------*/
		MemobusRespTrnsStart( Hmsg, 7, RESMT_COMPLETE );
		return;
}


/**********************************************************************************************/
/* Function_Name: MemobusReadRegister         */
/* Description  : Modbus读取处理
                  调用：Modbus协议处理函数中执行
                  输入：
                  输出：   */
/**********************************************************************************************/
#define	SMBUS_MIN_REGNUM	  1
void	MemobusReadRegister( HMSGIF Hmsg, LONG FuncCodeIdx )
{
LONG	rc;
USHORT	*ResBuf = (USHORT *)(Hmsg->ResBuf);
USHORT	*pResData;
USHORT	*CmdBufW = (USHORT *)(Hmsg->CmdBuf);
ULONG	RegNo;
USHORT	RegNum;
USHORT	RegBnum;
USHORT	TrnsLen;
USHORT	MaxRegNum;

/*--------------------------------------------------------------------------------------------------*/
/*** SndBufSize配置为256字节，减去固定长度部分6并保留裕量到10，除以2，就算出来最大寄存器个数 ***/
/*--------------------------------------------------------------------------------------------------*/
		MaxRegNum = (Hmsg->SndBufSize - 10) >> 1;

        /* 取功能码地址,两个字节合并成一个字 */
		RegNo = (ULONG)RpiWordSwap( (UCHAR *)&(Hmsg->CmdBuf[ 2 ]) );
        /* 读取寄存器的数量,单位为字,两个字节合并成一个字 */
		RegNum = RpiWordSwap( (UCHAR *)&(Hmsg->CmdBuf[ 4 ]) );  //寄存器个数
        
		RegBnum = 2 * RegNum;   /* 字节个数 */

		/*** 读取寄存器数量不在范围内，返回错误帧 ***/
		if ( (RegNum < SMBUS_MIN_REGNUM) || (RegNum > MaxRegNum) )
		{
			MemobusSendErrorResponse( Hmsg, SMBUS_EC_DANUMERR ); // 返回个数异常
			return;
		}
        
        
/*--------------------------------------------------------------------------------------------------*/
/*		The register read processing																*/
/*--------------------------------------------------------------------------------------------------*/
		pResData = &ResBuf[ 3 ];                    /*  取发送的缓存的地址指针,用来保存需要发送的数据           */

        /*------------------------------------------------------------------------------------------*/
        /* 特定功能应答处理                                                                             */
        /*------------------------------------------------------------------------------------------*/
        if( RegNo == 0xBFA0 )
        {
            ARM_IC_Response( Hmsg );  // 读取IC信息-ARM+FPGA芯片型号，并生成应答数据
            return;
        }
        else if( RegNo == 0xBFA0 && ResBuf[ 0 ] == 0x00)
        {
            ARM_Bord_Response( Hmsg );
        }
        else   /*** 执行读取函数部分 ***/
        {
            /*** 这里是通讯协议和功能码参数的的读取接口。这里的数据获取指针采用了16bit类型，
                    获得的数据是按照字的方式存储在数组中的：低地址字节是低8位，高地址字节是高8位。 ***/
            rc = RegFuncTbl[ RFTBLIDX(RegNo) ].ReadRegister( RegNo, RegNum, (USHORT *)pResData, Hmsg );        
        }        
        
/*--------------------------------------------------------------------------------------------------*/
/*		 寄存器读取异常检查 */
/*--------------------------------------------------------------------------------------------------*/
		if ( (rc != REGIF_COMPLETE) && (rc != REGIF_CONTINUE) )
		{
			MemobusSendErrorResponse( Hmsg, rc );
			return;
		}

        /*** 把查询到功能码(Word)拆分成字节 ***/
		ResBuf[0] = CmdBufW[0];					/*  轴号和功能码						    */
		ResBuf[1] = CmdBufW[1];                 /*  AxisAddr, FunctionCode                          */
        ResBuf[2] = (UCHAR)RegBnum;             // 计算返回字节个数

		TrnsLen = 5 + RegBnum;                  /*  发送字节长度                                       */
/*--------------------------------------------------------------------------------------------------*/
/*		CRC计算																					    */
/*--------------------------------------------------------------------------------------------------*/
		MlibSetCRC16( Hmsg->ResBuf, TrnsLen );
/*--------------------------------------------------------------------------------------------------*/
/*		响应数据传输设置																			    */
/*--------------------------------------------------------------------------------------------------*/
		MemobusRespTrnsStart( Hmsg, TrnsLen, RESMT_COMPLETE );

		return;
}

/**********************************************************************************************/
/* Function_Name: MemobusWriteRegister         */
/* Description  : Modbus写入处理
                  调用：Modbus协议处理函数中执行
                  输入：
                  输出：   */
/**********************************************************************************************/
void	MemobusWriteRegister( HMSGIF Hmsg, LONG FuncCodeIdx )
{
LONG	rc;
ULONG	i;
//USHORT	*CmdBuf = (USHORT *)(Hmsg->CmdBuf);
USHORT	*pCmdData;
USHORT	*CmdBufW = (USHORT *)(Hmsg->CmdBuf);
USHORT	*ResBufW = (USHORT *)(Hmsg->ResBuf);
ULONG	RegNo;      // 数据地址，并在RegFuncTbl[]中查找具体哪种寄存器写入
USHORT	RegNum;     // 写word个数
USHORT	RegBnum;    // 写byte个数 = 2*RegNum
USHORT	TrnsLen;
USHORT	DataTmp;

USHORT  DataBuf[128];

/*--------------------------------------------------------------------------------------------------*/
/*		取功能码地址,两个字节合并成一个字																				    */
/*--------------------------------------------------------------------------------------------------*/
		RegNo = (ULONG)RpiWordSwap( (UCHAR *)&(Hmsg->CmdBuf[ 2 ]) );

/*--------------------------------------------------------------------------------------------------*/
/*		不同写入指令处理																				    */
/*--------------------------------------------------------------------------------------------------*/
		if ( Hmsg->CmdBuf[ 1 ] == MODBUS_REG_WR )               /* 0x06指令:单个字写入                         */
		{
			RegNum = SMBUS_MIN_REGNUM;                          /* 写入字数                           */
			RegBnum = 2 * RegNum;                               /* 写入字节数                          */
			pCmdData = &DataBuf[ 0 ];                           // 写入数据的起始地址
		}
		else                                                    /* 0x10 指令:写入多个字                        */
		{                                                       /* 写入字数                         */
			RegNum = Hmsg->CmdBuf[ 5 ];
            if( Hmsg->CmdBuf[ 6 ] == 0 )                       // 字节数为0，代表258个字节
            {
                RegBnum = 258;
            }
            else
            {
                RegBnum = Hmsg->CmdBuf[ 6 ];                        /* 写入字节数                       */
            }
            pCmdData = &DataBuf[ 0 ];                           // 写入数据的起始地址
		}
/*--------------------------------------------------------------------------------------------------*/
/*** 写入字的个数合法性检查
    这里是最小写入1和(字节数、字数)对等的检查。没有对最长写入做限制，但是很显然，最长是258个字节。***/
/*--------------------------------------------------------------------------------------------------*/
        if ( (RegNum < SMBUS_MIN_REGNUM ) || (RegBnum != 2 * RegNum) )
		{
			MemobusSendErrorResponse( Hmsg, SMBUS_EC_DANUMERR );  //返回错误帧
			return;
		}
/*--------------------------------------------------------------------------------------------------*/
/*** 将功能码word：低地址字节是低8位，高地址字节是高8位；拆分成modbus协议字节：低地址字节是
    高8位，高地址字节是低8位。实现了高低字节对调，成功将协议字节顺序转换成word顺序。 ***/
/*--------------------------------------------------------------------------------------------------*/
		for ( i = 0; i < RegNum; i++ )
		{
            if( Hmsg->CmdBuf[ 1 ] == MODBUS_REG_WR)
            {
                DataTmp = ((Hmsg->CmdBuf[ 4 ]) << 8)  + Hmsg->CmdBuf[ 5 ];
            }
            else
            {
                DataTmp = ((Hmsg->CmdBuf[ 7 + 2* i ]) << 8)  + Hmsg->CmdBuf[ 8 + 2* i ];
            }
            *(USHORT*)(pCmdData + i) = DataTmp;
		}

		/*** Modbus通讯协议和功能码的写入接口 ***/
		rc = RegFuncTbl[ RFTBLIDX(RegNo) ].WriteRegister( RegNo, RegNum, (USHORT *)pCmdData, Hmsg );
/*--------------------------------------------------------------------------------------------------*/
/*		寄存器写入异常检查																*/
/*--------------------------------------------------------------------------------------------------*/        
		if ( (rc != REGIF_COMPLETE) && (rc != REGIF_CONTINUE) )
		{
			MemobusSendErrorResponse( Hmsg, rc );
			return;
		}
/*--------------------------------------------------------------------------------------------------*/
/*		数据写入处理完成后，恢复协议数组数据          																        */
/*--------------------------------------------------------------------------------------------------*/         
		for ( i = 0; i < RegNum; i++ )
		{
			*(USHORT*)(pCmdData + 2*i) = RpiWordSwap( (UCHAR *)(pCmdData + 2*i) );
		}
        
		/*** 继续情况下，直接返回，不用回复数据 ***/
		if( rc == REGIF_CONTINUE )
		{
			return;
		}

		ResBufW[0] = CmdBufW[0];
		ResBufW[1] = CmdBufW[1];
		ResBufW[2] = CmdBufW[2];
		ResBufW[3] = CmdBufW[3];
		ResBufW[4] = CmdBufW[4];
		ResBufW[5] = CmdBufW[5];
		TrnsLen = 8;                                // 06和10写入命令的应答数据均为8，前六个字节和接收数据一致。s
		MlibSetCRC16( Hmsg->ResBuf, TrnsLen );      // 计算CRC校验

		MemobusRespTrnsStart( Hmsg, TrnsLen, RESMT_COMPLETE );   // 响应数据传输设置
		return;
}



/**********************************************************************************************/
/* Function_Name: RpxSmsgMainProcedure         */
/* Description  : Modbus 协议处理
                  调用：Usb或串行通讯消息管理函数中调用
                  输入：
                  输出：   */
/**********************************************************************************************/
void	RpxSmsgMainProcedure( HMSGIF Hmsg )
{
        /*------------------------------------------------------------------------------------------*/
        /*		通讯处理用相关变量         														            */
        /*------------------------------------------------------------------------------------------*/
		Hmsg->FunSts  = 0;                              /* 辅助功能执行结果							    */
		Hmsg->LstErrRno = Hmsg->ErrRno;                 /* 上回异常代码      						    */
		Hmsg->ErrRno  = 0xFFFF;                         /* 异常代码    							    */
		MbusMngV.ErrReg = Hmsg->LstErrRno;              /* 寄存器参考用异常寄存器编号			        */
        
        /*------------------------------------------------------------------------------------------*/
        /*		Memobus 命令处理																	    */
        /*------------------------------------------------------------------------------------------*/
		switch( (UCHAR)Hmsg->CmdBuf[1] )
		{
            /*--------------------------------------------------------------------------------------*/
			case MODBUS_LOOPBACK:   /* 0x08 回送测试    */
				MemobusLoopbackTest(Hmsg, 0);
				break;
            /*--------------------------------------------------------------------------------------*/
			case MODBUS_REG_RD:     /* 0x03读N个字      */
			case 0x04:
				MemobusReadRegister(Hmsg, 24);
				break;
            /*--------------------------------------------------------------------------------------*/
			case MODBUS_REG_WR:     /* 0x06 写一个字    */
			case MODBUS_PREG_WR:    /* 0x10 写N个字     */
				MemobusWriteRegister(Hmsg, 25);
				break;
            /*--------------------------------------------------------------------------------------*/
            //case MODBUS_MEM_RD:     /* 0x50 寄存器读 */     待完善 2017.08.10
				//MemobusReadMemory(Hmsg, 25);
			//	break;
            /*--------------------------------------------------------------------------------------*/
			default:
				MemobusSendErrorResponse( Hmsg, SMBUS_EC_FNCDERR );  // 返回错误帧
            /*--------------------------------------------------------------------------------------*/
		}
		return;
}



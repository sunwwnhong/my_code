#include "Main.h"

#ifdef CPU1
#include "applInterface.h"
#endif

SYSTEMVERSION SystemVar;
KPRMDAT Kprm;
void SystemMain_Init( void );
void SystemMain_Loop( void );




int main(void)
{
    //---------------------------------------------------------------------------------------------
    // 1.Initializes device clock and peripherals
    //---------------------------------------------------------------------------------------------
    Device_init( );

    //---------------------------------------------------------------------------------------------
    // 3.Initializes PIE and clears PIE registers. Disables CPU interrupts.
    //---------------------------------------------------------------------------------------------
    Interrupt_initModule();

    //---------------------------------------------------------------------------------------------
    // 4.Initializes the PIE vector table with pointers to the shell Interrupt
    // Service Routines (ISR).
    //---------------------------------------------------------------------------------------------
    Interrupt_initVectorTable();

    //---------------------------------------------------------------------------------------------
    // 5. CPU 1 & CLA1 Init
    //---------------------------------------------------------------------------------------------
    InitCPU10( );                                            // CPU1 工作
    InitCLA1( );                                             // 协处理器开启

    //---------------------------------------------------------------------------------------------
    // 6. User Initial
    //---------------------------------------------------------------------------------------------
    SystemMain_Init( );

    //---------------------------------------------------------------------------------------------
    // 7. Enable Global Interrupt (INTM) and realtime interrupt (DBGM)
    //    Service Routines (ISR).
    //---------------------------------------------------------------------------------------------
    InitCpu1_Start( );
    Interrupt_enableMaster();

    //---------------------------------------------------------------------------------------------
    // 8. Loop Forever
    //---------------------------------------------------------------------------------------------
    while(1)
    {

#ifdef ETHERCAT_STACK
        MainLoop();
#endif
        SystemMain_Loop( );
    }
}


//*************************************************************************************************
//
// 系统初始化部分
//
//*************************************************************************************************
void SystemMain_Init( void )
{
    //---------------------------------------------------------------------------------------------
    AlarmManager_Init();                                // 报警处理
    StateMachine_Init();                                // 状态机变量初始化为 0
    VarDef_Init();                                      // 系统专用变量初始化为 0
    CLAVar_Init();                                      // CLA 相关数据清零
    //---------------------------------------------------------------------------------------------
    RelayControl_Init();                                // 特殊位初始化
    DBControl_Init();                                   // 动态制动控制初始化
    MotorBK_Init( );                                    // 抱闸信号初始化
    //---------------------------------------------------------------------------------------------
    MlibResetLONGMemory( &KnlV, sizeof( KnlV ) / 2 );   // 数据清零
    MlibResetLONGMemory( &KnxV, sizeof( KnxV ) / 2 );   // 数据清零
    //---------------------------------------------------------------------------------------------
    InitParameterFromEeprom( );                         //从Eeprom中读取系统参数
    IprmcalBitParam000( );


    //---------------------------------------------------------------------------------------------
    //                                    功能版本读取(ARM + FPGA)
    //---------------------------------------------------------------------------------------------
    SystemVar.McuVer        = ARM_Version;              // ARM 主版本
    SystemVar.FpgaVer       = 3400;                     // FPGA 主版本
    SystemVar.FpgaSubVer    = 0;                        // FPGA 副版本
    SystemVar.OrderID       = ARM_Sub_Version;          // ARM 副版本


    //---------------------------------------------------------------------------------------------
    Kprm.AmpType = ( Prm.syssw1 & 0x0002 ) ? AMPTYPE_LINEAR : AMPTYPE_ROTARY;
    //---------------------------------------------------------------------------------------------
    InitSerialMsg( );                                   // 串口初始化
    InitCPU11( );                                       // 外设配置(与用户参数关联,需要读取参数后配置)
    DpiInitUsbMbusMsg();                                //USB 相关变量初始化清零处理
    RpxInitUsbMsgInterface();
    //---------------------------------------------------------------------------------------------
    RpxInitMonManager( );                       /* 监控功能管理初始化                               */
    RpxInitDtrcManager( );                      /* 数据采集管理部分初始化                           */
    RpxPnlOpMonInit( );                         /* 默认监视参数初始化                               */
    User20_DIDO_Init( );                        /* SI0 ~ SI9 输入映射配置处理                       */
    //---------------------------------------------------------------------------------------------
    if( Kprm.f.CanOpenNet == 1 )
    {
        CAN_Init( );                            /* CAN相关变量初始化处理                            */
    }
    //---------------------------------------------------------------------------------------------
    ODBMange_Init( );                           /* CanOpen对象字典管理初始化                        */
    PrModeVarInit( );                           /* Pr 模式相关变量清零                              */
    //---------------------------------------------------------------------------------------------

    Mot00_Main_Init();                          /* 电机控制算法初始化                               */
    RpxInitRegManager();                        /* 寄存器操作管理初始化                             */

    AlarmManager();                             /* 故障管理开启                                     */
    KpxCheckAmpMotType( );                      /* 驱动器和电机的电压,容量匹配度检测                */

    //---------------------------------------------------------------------------------------------
    UserD_Init( );                              /* 特殊功能初始化                                   */
    //---------------------------------------------------------------------------------------------
    //                          用户权限,临时测试,后续删除(待完善)
    //---------------------------------------------------------------------------------------------
    //  = 1 :User 1  【default】
    //  = 2 :User 2
    //  = 3 :System
    //---------------------------------------------------------------------------------------------
    PopMsg.AccsLvl  = ACCLVL_USER1;
    SrlMsg.AccsLvl  = ACCLVL_USER1;
    UsbMsg.AccsLvl  = ACCLVL_USER1;
    OpeReg.AccLevel = REG_ACCLVL_USER1;

    //---------------------------------------------------------------------------------------------
    //  Access.inhibit :   1   禁止写入
    //                     0   允许写入
    Access.inhibit = Iprm.f.EepPrmWrite ? 1 : 0;
    //---------------------------------------------------------------------------------------------
    //  参数辨识测试  @2020.04.25
    //---------------------------------------------------------------------------------------------
#if 0
    Estimate_Init( );
    TlObs_Init( );
#endif
    TlObs_Init( );
    //---------------------------------------------------------------------------------------------
    /* 内部强制使能是否掉电保存,不保存则清除相关标志位                                              */
    //---------------------------------------------------------------------------------------------
    if( (Prm.b_prm0_1 & 0x0010) == 0x0000 )
    {
        Prm.b_prm0_1 = 0x0000;
    }

    //---------------------------------------------------------------------------------------------
    InitC1_EtherCAT( );

    EtherCAT_Init( );                           /* EtherCAT 相关寄存器变量初始化                    */
    //---------------------------------------------------------------------------------------------
    fun_SinCos_ADC_Init();  //自解码参数初始化
    //KTH78_Init( );                              /*昆泰芯KTH7812初始化   //@20250814 yjp 硬件:PG40\PG20 */
}

//*************************************************************************************************
//
// 普通2ms LOOP
//
//*************************************************************************************************
volatile ULONG CheckCcnt = 0;
void SystemMain_Loop( void )
{
ULONG StartTime;
    //---------------------------------------------------------------------------------------------
    if( CheckCcnt == KnlV.ScanCcnt )
    {
        return;                     // 2ms 执行一次
    }
    //---------------------------------------------------------------------------------------------
    //  LOOP 环路 开始
    //---------------------------------------------------------------------------------------------
    KPX_RST_SCANR_STRTIME( StartTime );                     // LOOP 执行时间计时开始

    //---------------------------------------------------------------------------------------------
    //                                      EtherCAT 测试用
    //---------------------------------------------------------------------------------------------
    EtherCAT_2msLoop( );                    // EtherCAT 2ms 循环处理部分
    UserD_TaskR( );                         // 特殊功能处理部
    FactoryTest( );                         // 工厂测试
    //---------------------------------------------------------------------------------------------
    //      Round Timer Service,产生100ms的标志信号,该信号在下一个2ms时间置零,在ScanC中最好不要使用该位
    //---------------------------------------------------------------------------------------------
    if( KnlV.Roundcnt >= 50 )
    {
        KnlV.Roundcnt = 0;                 // Reset Round Timer
        KnlV.f.Pulse100ms = ON;            // 100ms Pulse ON
    }
    else
    {
        KnlV.f.Pulse100ms = OFF;           // 100ms Pulse OFF
    }

    RinV.f.BaseEnable = ( CoutV.f.Sv.BaseEnable ) ? TRUE : FALSE;
    //---------------------------------------------------------------------------------------------
    KpxScanWriteEepromQueue();             // Eeprom 存储队列处理
    //---------------------------------------------------------------------------------------------
#if 0
    PanOperManager();                      // 键盘操作管理
#endif
    //---------------------------------------------------------------------------------------------
    SerialMsgManager();                    // 串口接收信息处理
    RpxUsbMsgManager( );                   // USB处理部分
    //---------------------------------------------------------------------------------------------
    RpxFunExecute( );                      // 辅助功能执行过程
    RpxAnotchRound( );                     // 免调整陷波处理
    //---------------------------------------------------------------------------------------------
    MEncClrDeal();                         // 多圈绝对值数据清除
    MRevDealInit( );                       // 多圈数据上限初始化
    //---------------------------------------------------------------------------------------------
    LpxSoftReset( );                       // 软件复位Soft ReSet
    //---------------------------------------------------------------------------------------------
    MotorInfWriteToEnc();                  // 电机信息写入到编码器的Eeprom中
    MRRevCntSaved( );
    EncodeOrgSet( );
    SnHoldUpdate();
    CTek5900PosBResret();
    CorrectSave();
    CompensateWriteEnable();


    //---------------------------------------------------------------------------------------------
#ifdef CPU1
    if( A1ToC1V.ACRUpdateErr >= 5 )        // CLA 负荷监控
    {
        KPI_ALARMSET( ALM_SAMP_OVER );
    }
#endif
    //---------------------------------------------------------------------------------------------
    KnlV.Roundcnt++;                       // Increment Round Execute Counter
    //---------------------------------------------------------------------------------------------
    RoutV.f.RndReady = TRUE;               // 置位普通循环运行标志
    //---------------------------------------------------------------------------------------------
    KpxEgearCheck( );                      // 电子齿轮比范围检查
    //---------------------------------------------------------------------------------------------
    GainSel_Update( );
    //---------------------------------------------------------------------------------------------
    DSP_Update( );                         // DSP 软件升级
    ErrFlag_Update( );                     // 信号指示灯显示处理
    CheckCcnt = KnlV.ScanCcnt;             // 更新此次的比较值,便于下次使用(2017.06.29 修复Bug)
    //---------------------------------------------------------------------------------------------
    // Round 计时结束
    //---------------------------------------------------------------------------------------------
    KPX_GET_SCANR_EXETIME( StartTime );
}

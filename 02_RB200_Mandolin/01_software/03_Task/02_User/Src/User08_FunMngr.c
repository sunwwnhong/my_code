void FunRegCvtCode(FUNEXE *ExeTbl)
{
	switch(ExeTbl->App.FMsgCmd)
	{
        case 0x0000:
            ExeTbl->App.CmdCode = FCMD_STOP;
            break;
        case 0x0001:
            ExeTbl->App.CmdCode = FCMD_EXEC;
            break;
        case 0x0002:
            ExeTbl->App.CmdCode = FCMD_UP;
            break;
        case 0x0003:
            ExeTbl->App.CmdCode = FCMD_DOWN;
            break;
        case 0x0004:
            ExeTbl->App.CmdCode = FCMD_SV;
            break;
        case 0x0005:
            ExeTbl->App.CmdCode = FCMD_CH1;
            break;
        case 0x0006:
            ExeTbl->App.CmdCode = FCMD_CH2;
            break;
        case 0x0007:
            ExeTbl->App.CmdCode = FCMD_EWRT;
            break;
        case 0x0008:
            ExeTbl->App.CmdCode = FCMD_EXEC2;
            break;
        case 0x0011:
            ExeTbl->App.CmdCode = FCMD_MTEXEC;
            break;
        case 0x0012:
            ExeTbl->App.CmdCode = FCMD_MTUP;
            break;
        case 0x0013:
            ExeTbl->App.CmdCode = FCMD_MTDOWN;
            break;
        default:
            ExeTbl->App.CmdCode = FCMD_SGNONE;
            break;
        }
        ExeTbl->App.FKeepCmdCode = FALSE;
}








/*Function_Name:KpiInputSpdRef                                                                 		*/
/*Description  :速度指令			                                                                    */
/*																									*/
/****************************************************************************************************/
LONG KpiInputSpdRef(void)
{
LONG SpeedTmp = 0;
UCHAR SpeedSel;
    //---------------------------------------------------------------------------------------------
    //  CanOpen 机型
    //---------------------------------------------------------------------------------------------
    if( Kprm.f.CanOpenNet == 1 )
    {
        SpeedTmp = CanOpenSpdSource();
    }
    else
    {
        //-----------------------------------------------------------------------------------------
        // 速度指令源选择
        //-----------------------------------------------------------------------------------------
        switch( Prm.spdSource & 0x000F )
        {
            case 0x0000:        // 内部数字给定
                SpeedTmp = (LONG)((SHORT)Prm.speed0) * 10L;
                break;
            case 0x0007:
#ifdef INNER_INPUT_FUNC
                SpeedTmp = InnerInputCal(&SpdSig);
#endif
                break;
            case 0x0003:        // 485通讯专用
                SpeedTmp = (LONG)(MuxAxis485_SpdCmd()) * 10L;
                break;
            //-------------------------------------------------------------------------------------
            // 端子控制
            //-------------------------------------------------------------------------------------
            case 0x0004:
                SpeedSel = ((UCHAR)CinV.f.Sv.SpdB << 1) | CinV.f.Sv.SpdA;
                switch( SpeedSel & 0x03 )
                {
                    case 0:
                        SpeedTmp = (LONG)((SHORT)Prm.speed0) * 10;
                        break;
                    //-----------------------------------------------------------------------------
                    case 1:
                        SpeedTmp = (LONG)((SHORT)Prm.speed1) * 10;
                        break;
                    //-----------------------------------------------------------------------------
                    case 2:
                        SpeedTmp = (LONG)((SHORT)Prm.speed2) * 10;
                        break;
                    //-----------------------------------------------------------------------------
                    case 3:
                        SpeedTmp = (LONG)((SHORT)Prm.speed3) * 10;
                        break;
                    //-----------------------------------------------------------------------------
                    default:
                        SpeedTmp = 0;
                        break;
                }
                break;
            //-------------------------------------------------------------------------------------
            default:
                KpiSetParamError( &pndef_spdSource );       // 参数设置异常
                break;
        }
    }
    
    /*----------------------------------------------------------------------------------------------*/
    /*                             速度方向调整(外部端子 + 内部寄存器)                              */
    /*----------------------------------------------------------------------------------------------*/
    if( CinV.f.Sv.SpdD )                            /* 外部输入端子调整方向                         */
        SpeedTmp = -SpeedTmp;
    if(( Prm.spdcmd & 0x0001 ) == 0x0001)           /* 内部寄存器调整速度方向                       */
        SpeedTmp = -SpeedTmp;
    /*----------------------------------------------------------------------------------------------*/
    SpeedTmp = KpiSpeedLevelCal( SpeedTmp , 0 );    /*  0.1rpm                                      */

    /*----------------------------------------------------------------------------------------------*/
    /*  最高运行速度限制( Pn316 )                                                                   */
    /*----------------------------------------------------------------------------------------------*/
    SpeedTmp = MlibLimitul(SpeedTmp,  Iprm.MaxSpd,    -Iprm.MaxSpd);
    
    return SpeedTmp;
}






/**********************************************************************************************/
/* Function_Name: DpxSetSifMbusTrnsStart         */ 
/* Description  : Modbus开始应答指令(通知通信时序中开始发送数据)(Round用)
                  调用：USB/串口通讯开始应答指令-函数调用
                  输入：应答长度(Byte)、应答类型(应答/不应答)
                  输出： 	*/
/**********************************************************************************************/
UCHAR	DpxSetSifMbusTrnsStart(
		USHORT	MsgLength,								// 应答长度
		USHORT	MsgType	)								// 应答类型
{
UCHAR	rc = COMDEV_UNDEF;
/*--------------------------------------------------------------------------------------------------*/
/*		消息处理时序：SQSTEP_WAITSTX状态时进行应答处理     				    						*/
/*--------------------------------------------------------------------------------------------------*/
		if( Sif.SqStep == SQSTEP_WAITSTX )
		{
            /*--------------------------------------------------------------------------------------*/
            /*	    已经字节长度合法性检查：错误返回帧数据长度为5，也是满足条件的		    		*/
            /*--------------------------------------------------------------------------------------*/
			if(( MsgLength > (USHORT)MBUS_TRN_MAXSIZEB ) || ( MsgLength < 5 ) )
			{
                //---------------------------------------------------------------------------------
                //		发送信息类型检查
                //---------------------------------------------------------------------------------
			    Sif.ReqStx  = SIFREQ_NOANSWER;          // 禁止发送请求
			    Sif.SqStep = SQSTEP_WAITSTX2;           // 进入到下一个等待阶段
				rc = COMDEV_NGSIZE;
			}
			else
			{
                /*----------------------------------------------------------------------------------*/
                /*                      															*/
                /*----------------------------------------------------------------------------------*/
				Sif.MbusTrnLength = MsgLength;
                /*----------------------------------------------------------------------------------*/
                /*		发送信息类型检查         													*/
                /*----------------------------------------------------------------------------------*/
				switch( MsgType )
				{
					case RESMT_COMPLETE :				// 消息处理完毕,开始应答
						Sif.ReqStx  = SIFREQ_STARTTX;	// 启动发送请求设定
						rc = COMDEV_OK;					
						break;
					case RESMT_NORESPONSE :				// 消息异常,无需应答
						Sif.ReqStx  = SIFREQ_NOANSWER;	// 禁止发送请求
						rc = COMDEV_OK;					
						break;
					default :
						rc = COMDEV_UNDEF;				// 消息类型异常(未定义)
						break;
				}
				Sif.SqStep = SQSTEP_WAITSTX2;			// 进入到下一个等待阶段
			}
		}
		else
		{
			rc = COMDEV_NGTIME;                         // 未到达发送时机
		}

		return( rc );
}

/**********************************************************************************************/
/* Function_Name: LpxSifMbusChkMyMsg         */ 
/* Description  : 判断消息是否发给本站     
                  调用：串行通讯实时监控-函数中调用
                  输入：
                  输出： 	*/
/**********************************************************************************************/
ULONG LpxSifMbusChkMyMsg( void )
{
        //------------------------------------------------------------------------------------------
        //		消息地址为广播地址"0"或者为本站地址
        //------------------------------------------------------------------------------------------
		if(( Sif.RxBuf[0] == 0x00 )||( Sif.RxBuf[0] == Sif.AxisNumber ))
		{
			return( TRUE );
		}
		else
		{
			return( FALSE );
		}
}

/**********************************************************************************************/
/* Function_Name: DpxGetSifMbusRcvACK         */ 
/* Description  : 判断Modbus消息是否接收完毕，并获取消息长度(Round用,判断是否开始处理消息) 
                  调用：判断通信端口是否接收数据完毕-函数中调用
                  输入：
                  输出： 	*/
/**********************************************************************************************/
UCHAR	DpxGetSifMbusRcvACK( USHORT *RcvbLength )
{
UCHAR	rc = FALSE;

		if( Sif.SqStep == SQSTEP_WAITSTX )
		{
			*RcvbLength = Sif.RcvbLength;			// 返回消息长度(字节数)
			rc = COM_ACK;							// 消息接收完成
		}
		else
		{
			rc = ALL_NAK;							// 消息未接收完成
		}
		return( rc );
}



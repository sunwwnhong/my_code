/* Function_Name: InitSerialMsg         */ 
/* Description  : UART串行通信协议初始化处理
                  调用：上电初始化调用一次（读取EEPROM后）
                  输入：
                  输出： 	*/
/**********************************************************************************************/
void	InitSerialMsg( void )
{

/*--------------------------------------------------------------------------------------------------*/
/*		串口通信数据初始化                                                                          */
        MlibResetLONGMemory( &Sif, sizeof( Sif ) / 2 );	/* Reset All Data to Zero                   */
/*--------------------------------------------------------------------------------------------------*/
/*      轴号设置：设置SCI和USB中的设备地址变量，实际没有用，后面设备号直接用的Para参数              */
        DpiComSetAxisNumber( COM0_UART, (UCHAR) (Prm.Address & 0x00FF) );
/*--------------------------------------------------------------------------------------------------*/
/*      串口通讯收发接口和modbus协议接口对接函数                                                    */
        LpxInitMsgInterface();
            
/*--------------------------------------------------------------------------------------------------*/
        SrlMsg.AccsLvl = 3;
            
		return;
}


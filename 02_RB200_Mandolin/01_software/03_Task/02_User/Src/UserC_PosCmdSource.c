#include "Main.h"

typedef struct  {
        UCHAR   PosStep;                                /* 位置指令步骤                               */
        ULONG   PosDelayCnt;                            /* 位置指令延时计时器                        */
        LONG    PosSum;                                 /* 位置指令数                               */
}POSCMD;
POSCMD PosCmd;


#define PulseSpd 15

/****************************************************************************************************/
/*                                                                                                  */
/*  位置指令生成器(正反转)                                                                          */
/*                                                                                                  */
/****************************************************************************************************/
LONG PosGenerator( void )
{
LONG kx = 0;
    /*----------------------------------------------------------------------------------------------*/
    /* 伺服未使能时退出                                                                             */
    /*----------------------------------------------------------------------------------------------*/
    if( !BinV.f.BaseEnable )
    {
        PosCmd.PosStep = 0;
        return 0;
    }
    /*----------------------------------------------------------------------------------------------*/
    switch( PosCmd.PosStep )
    {
        /*------------------------------------------------------------------------------------------*/
        case 0:
            PosCmd.PosSum = 0;
            PosCmd.PosStep = 1;
            break;
        /*------------------------------------------------------------------------------------------*/
        case 1:
            if( PosCmd.PosSum <= 40000UL )
            {
                kx = PulseSpd;
                PosCmd.PosSum += PulseSpd;
            }
            else
            {
                PosCmd.PosDelayCnt = 0;
                PosCmd.PosStep = 2;
            }
            break;
        /*------------------------------------------------------------------------------------------*/
        case 2:
            if( ++PosCmd.PosDelayCnt > 40000 )
            {
                PosCmd.PosStep = 3;
                PosCmd.PosDelayCnt = 0;
            }
            break;
        /*------------------------------------------------------------------------------------------*/
        case 3:
            if( PosCmd.PosSum > 0 )
            {
                kx = -(PulseSpd);
                PosCmd.PosSum += kx;
            }
            else
            {
                PosCmd.PosSum       = 0; // 修改
                PosCmd.PosDelayCnt  = 0;
                PosCmd.PosStep      = 4;
            }
            break;
        /*------------------------------------------------------------------------------------------*/
        case 4:
            if( ++PosCmd.PosDelayCnt > 40000 )
            {
                PosCmd.PosStep = 0;
                PosCmd.PosDelayCnt = 0;
            }
            break;
        /*------------------------------------------------------------------------------------------*/

        default:
            break;
    }
    return kx;
}




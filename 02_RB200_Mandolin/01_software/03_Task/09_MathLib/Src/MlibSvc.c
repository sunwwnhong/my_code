{
LONG	BitNo;
 		BitNo = MlibSrhbiton( x, SRH_FROM_LSB );
 		if( (BitNo >= 0) && (x == (ULONG)(1<<BitNo)) )
		{
			return( BitNo );
		}
		else
		{
			return( -1 );
		}
 }
 
 

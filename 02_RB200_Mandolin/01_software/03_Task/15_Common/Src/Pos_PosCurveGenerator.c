#include "Main.h"


#define INCPOS_I        0                      /*   正向加速 1象限加速运行                          */
#define DECPOS_I        1                      /*   正向减速 1象限减速运行                          */
#define INCNEG_IV       2                      /*   反向加速 4象限加速运行                          */
#define DECNEG_IV       3                      /*   反向减速 4象限减速运行                          */
#define DEC_I_INC_IVC   4                      /*   正向向负向转换 1象限运到到4象限                 */
#define DEC_IV_INC_I    5                      /*   负向向正向转换  4象限运到到1象限                */


POSCURVEGENERATOR_P PosCurvP;                                    /* 位置曲线规划曲线                */
POSCURVEGENERATOR_V PosCurvV;                                    /* 位置曲线规划曲线                */


/****************************************************************************************************/
/*Function Name: EthercatPosUpdate(void)                                                            */
/*Description  : EtherCAT 相关位置指令更新						                                    */
/*Attention    : 										                                            */ 
/****************************************************************************************************/
void EthercatPosParameterUpdate( void )
{
ULONG AccTmp;
ULONG DecTmp;
    /*----------------------------------------------------------------------------------------------*/
    /* 位置控制最大速度(p/s)   对象字典:  6081h                                                     */
    /*----------------------------------------------------------------------------------------------*/
    /*                6081 × 6091                                                                   */
    /* Speed(rpm) = --------------- × 60                                                            */
    /*                编码器分辨率                                                                  */
    /*----------------------------------------------------------------------------------------------*/
    /* 速度单位转换成脉冲单位                                                                       */
    /*               Speed(rpm) × 编码器分辨率                                                      */
    /* Speed(pps) = ---------------------------                                                     */
    /*                          60                                                                  */
    /*----------------------------------------------------------------------------------------------*/
    /* 简化后可以得到:                                                                              */
    /*                6081h × 6091h-1                                                               */
    /* Speed(pps) = -------------------------                                                       */
    /*                  6091h-2                                                                     */
    /*----------------------------------------------------------------------------------------------*/
    /* 需要将Speed(pps)转换成执行周期内脉冲数,执行周期为125us,则相应系数为:                         */
    /*           Speed(PPS) × 125            Speed(PPS)                                             */
    /* P/SCAN = ------------------------ = ------------                                             */
    /*           1000 × 1000   (S--->us)        8000                                                */
    /* 脉冲格式转换成Q15格式,于是有:                                                                */
    /*      2^15        2^9                                                                         */
    /*   ---------- = ------                                                                        */
    /*      8000        125                                                                         */
    /*----------------------------------------------------------------------------------------------*/
    PosCurvP.ProfRefSpdQ15 = (INT64)u32ProfileVel *  512 / 125;
 
    /*----------------------------------------------------------------------------------------------*/
    /* 位置控制轮廓加速时间   对象字典:  【0x6083】                                                 */
    /*----------------------------------------------------------------------------------------------*/
    /* 单位为 PPS,转换成扫描周期内脉冲数                                                            */
    /*        ACCP × 125 us                                                                         */
    /* ACC = --------------                                                                         */
    /*        1000 × 1000  (s -->us)                                                                */
    /* 转换成脉冲Q15格式,于是简化有:                                                                */
    /*    125 × 2^15        2^15      512                                                           */
    /* = -------------- = -------- = -----                                                          */
    /*     1000 × 1000      8000      125                                                           */
    /*----------------------------------------------------------------------------------------------*/
    /* 轮廓位置模式加速度限制处理                                                                   */
    /*----------------------------------------------------------------------------------------------*/
    if( u32ProfileAcc > u32MaxAcc )
    {
        AccTmp = u32MaxAcc;
    }
    else
    {
        AccTmp = u32ProfileAcc;
    }
    /*----------------------------------------------------------------------------------------------*/    
    if( u32ProfileDec > u32MaxDec )
    {
        DecTmp = u32MaxDec;
    }
    else
    {
        DecTmp = u32ProfileDec;
    }
    /*----------------------------------------------------------------------------------------------*/
    PosCurvP.AccBaseQ15 = (INT64)AccTmp * 8 / 15625;
    if(PosCurvP.AccBaseQ15 <= 1)
    {
        PosCurvP.AccBaseQ15 = 1;
    }
    /*----------------------------------------------------------------------------------------------*/
    /* 位置控制轮廓减速时间   对象字典:  【0x6084】                                                 */
    /*----------------------------------------------------------------------------------------------*/
    PosCurvP.DecBaseQ15 = (INT64)DecTmp * 8 / 15625;    
    if(PosCurvP.DecBaseQ15 <= 1)
    {
	        PosCurvP.DecBaseQ15 = 1;
    }
    /*----------------------------------------------------------------------------------------------*/
    /* 目标位置赋值                                                                                 */
    /*----------------------------------------------------------------------------------------------*/
    PosCurvV.PosRemain += PosCurvP.TargetPos;
    PosCurvV.PosSumIn  += PosCurvP.TargetPos;
    
    PosCurvV.TargetReach = 0;
}


/****************************************************************************************************/
